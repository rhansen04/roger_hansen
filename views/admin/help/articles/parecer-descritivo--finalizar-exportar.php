<p class="lead">Após completar o texto e as fotos, finalize o parecer descritivo para travá-lo contra edições e gere o documento PDF completo com 7 páginas para entrega às famílias.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Importante:</strong> O botão de exportação PDF só aparece quando o parecer está com status <strong>"Finalizado"</strong>. Revise todo o conteúdo antes de finalizar.
</div>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Finalize o parecer</h6>
            <p>Quando o texto e as fotos estiverem prontos, clique no botão <strong>"Finalizar"</strong> na página de visualização do parecer.</p>
            <ul>
                <li>Ao finalizar, todos os campos do parecer tornam-se <strong>somente leitura</strong></li>
                <li>O status muda para <strong>"Finalizado"</strong> (badge verde na listagem)</li>
                <li>Nenhuma edição é possível enquanto o parecer estiver finalizado</li>
                <li>A finalização indica que o professor considera o documento pronto para entrega</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Revisão pelo Coordenador</h6>
            <p>O coordenador pedagógico pode revisar o parecer finalizado e, se necessário, solicitar ajustes:</p>
            <ul>
                <li><strong>Aprovar:</strong> O coordenador visualiza o parecer completo e considera adequado — nenhuma ação adicional necessária</li>
                <li><strong>Solicitar Revisão:</strong> O coordenador clica em <strong>"Solicitar Revisão"</strong> e insere observações detalhando os pontos que precisam de ajuste</li>
                <li>Ao solicitar revisão, o status muda para <strong>"Revisão Solicitada"</strong> (badge amarelo)</li>
                <li>O professor é notificado sobre a solicitação de revisão</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Fluxo de revisão</h6>
            <p>Quando uma revisão é solicitada, o professor deve atender aos apontamentos:</p>
            <ul>
                <li>O parecer volta a ser <strong>editável</strong> para o professor</li>
                <li>As <strong>notas de revisão</strong> do coordenador ficam visíveis na página do parecer</li>
                <li>O professor faz as correções necessárias no texto e/ou fotos</li>
                <li>Após as correções, o professor clica novamente em <strong>"Finalizar"</strong> para reenviar</li>
                <li>O ciclo pode se repetir até que o coordenador esteja satisfeito com o resultado</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Exportar PDF</h6>
            <p>Com o parecer finalizado, o botão <strong>"Exportar PDF"</strong> aparece na página de visualização:</p>
            <ul>
                <li>Clique em <strong>"Exportar PDF"</strong> para gerar o documento</li>
                <li>O arquivo é baixado automaticamente com o nome <strong>parecer_{id}.pdf</strong></li>
                <li>O PDF pode ser gerado quantas vezes forem necessárias</li>
                <li>Recomenda-se verificar o PDF antes de compartilhar com as famílias</li>
            </ul>
        </div>
    </div>
</div>

<h5 class="mt-4 mb-3"><i class="fas fa-file-pdf me-2 text-danger"></i>Estrutura do PDF (7 páginas)</h5>
<p>O documento PDF gerado possui a seguinte estrutura padronizada:</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Capa</h6>
            <p>Página de capa com fundo verde institucional (<code>#007e66</code>), exibindo a foto do aluno centralizada, seu nome completo, a turma e o semestre de referência.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Página 1 — "Sobre o Parecer Descritivo"</h6>
            <p>Texto institucional fixo que apresenta às famílias o que é o Parecer Descritivo, sua finalidade pedagógica e como deve ser compreendido. Este texto é padrão para todos os pareceres.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Página 2 — Narrativa da Criança</h6>
            <p>Página com o nome do aluno como título, seguido do texto narrativo personalizado (campo student_text ou student_text_edited, se houver correção por IA). Este é o conteúdo principal do parecer.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Páginas 3 a 7 — Eixos com Fotos</h6>
            <p>Uma página para cada eixo de atividade, contendo:</p>
            <ul>
                <li><strong>Título do eixo</strong> com destaque visual (barra verde)</li>
                <li><strong>3 fotos</strong> dispostas em layout 2+1 (duas na parte superior, uma centralizada abaixo)</li>
                <li><strong>Legendas</strong> abaixo de cada foto, descrevendo o momento registrado</li>
                <li>A ordem dos eixos segue: Atividade Musical, Atividade Manual, Atividade de Contos, Atividade de Movimento, Programa Comunicação Ativa</li>
            </ul>
        </div>
    </div>
</div>

