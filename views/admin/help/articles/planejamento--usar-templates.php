<p class="lead">Templates são modelos reutilizáveis que definem a estrutura do planejamento pedagógico. Aprenda a escolher, personalizar e aproveitar ao máximo.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Você sabia?</strong> Cada template define <strong>seções</strong> (ex: Campos de Experiência, Objetivos) e dentro de cada seção, <strong>campos</strong> personalizados (texto, seleção, checkboxes). Quando você cria um planejamento, o formulário é montado automaticamente com base no template escolhido.
</div>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Acesse os Templates</h6>
            <p>No menu lateral, vá em <strong>Ensino → Templates Planej.</strong> Você verá os templates disponíveis organizados em cards com:</p>
            <ul>
                <li><strong>Nome</strong> — ex: "Planejamento PFI - Berçário"</li>
                <li><strong>Faixa etária</strong> — badge indicando o público-alvo (0-3 anos, 3-6 anos, etc.)</li>
                <li><strong>Status</strong> — Ativo (verde) ou Inativo (cinza)</li>
                <li><strong>Descrição</strong> — resumo do que o template abrange</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Criar um novo Template</h6>
            <p>Clique em <strong>"Novo Template"</strong> e preencha:</p>
            <ul>
                <li><strong>Nome:</strong> Título descritivo (ex: "PFII - Maternal II")</li>
                <li><strong>Descrição:</strong> Breve explicação do uso</li>
                <li><strong>Faixa etária:</strong> Público-alvo do template</li>
                <li><strong>Status:</strong> Ativo (aparece na seleção) ou Inativo (oculto)</li>
            </ul>
            <p>Após salvar, você será redirecionado para adicionar seções e campos.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Adicionar Seções ao Template</h6>
            <p>Seções são blocos temáticos do planejamento. Exemplos comuns:</p>
            <ul>
                <li>"Campos de Experiência"</li>
                <li>"Objetivos de Aprendizagem"</li>
                <li>"Metodologia"</li>
                <li>"Recursos Necessários"</li>
                <li>"Avaliação"</li>
            </ul>
            <p>Cada seção pode ter um tipo e ordem de exibição configuráveis.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Adicionar Campos às Seções</h6>
            <p>Dentro de cada seção, defina os campos que os professores preencherão. Tipos disponíveis:</p>
            <ul>
                <li><strong>Texto simples</strong> — campo de uma linha</li>
                <li><strong>Área de texto</strong> — campo multilinha para descrições longas</li>
                <li><strong>Seleção</strong> — dropdown com opções predefinidas</li>
                <li><strong>Radio</strong> — escolha única entre opções</li>
                <li><strong>Checkbox</strong> — múltipla escolha (checklist)</li>
            </ul>
            <p>Para campos de seleção, radio ou checkbox, defina as opções separadas por linha.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Usar o Template ao criar Planejamentos</h6>
            <p>Quando um professor cria um novo planejamento em <strong>Ensino → Planejamentos</strong>, ele seleciona o template desejado. O formulário é gerado automaticamente com todas as seções e campos definidos.</p>
        </div>
    </div>
</div>

<div class="help-tip help-tip-success">
    <i class="fas fa-check-circle me-2"></i>
    <strong>Dica profissional:</strong> Templates inativos não aparecem na seleção ao criar planejamentos. Use isso para manter templates antigos disponíveis sem confundir os professores.
</div>
