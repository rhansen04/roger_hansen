<p class="lead">O dashboard do coordenador oferece uma visão panorâmica de todas as turmas, professores e atividades pedagógicas. Use-o para supervisionar o trabalho da equipe, acompanhar o progresso dos cursos e identificar pontos de atenção rapidamente.</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>8 Cards de Métricas no Topo</h6>
            <p>O dashboard do coordenador exibe 8 cards com indicadores abrangentes:</p>
            <ul>
                <li><strong>Turmas Ativas:</strong> total de turmas em funcionamento no ano letivo atual</li>
                <li><strong>Total Crianças:</strong> quantidade de alunos matriculados em todas as turmas</li>
                <li><strong>Total Professores:</strong> número de professores ativos na plataforma</li>
                <li><strong>Observações:</strong> total de observações pedagógicas registradas no período</li>
                <li><strong>Total Escolas:</strong> quantidade de escolas cadastradas e ativas</li>
                <li><strong>Cursos Ativos:</strong> número de cursos de formação disponíveis</li>
                <li><strong>Matrículas:</strong> total de matrículas ativas em cursos</li>
                <li><strong>Progresso Médio:</strong> média percentual de conclusão dos cursos por todos os alunos matriculados</li>
            </ul>
            <p>Cada card é clicável e leva diretamente ao módulo correspondente para uma análise mais detalhada.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Relatório de Cursos</h6>
            <p>Na área principal, uma tabela apresenta o desempenho dos cursos de formação:</p>
            <ul>
                <li><strong>Nome do curso:</strong> título do curso de formação continuada</li>
                <li><strong>Matriculados:</strong> quantidade de professores/alunos matriculados</li>
                <li><strong>Progresso médio:</strong> barra visual colorida indicando o percentual médio de conclusão dos matriculados</li>
            </ul>
            <p>Este relatório permite identificar cursos com baixa adesão ou pouco progresso, orientando ações de incentivo junto à equipe.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Observações Recentes — Todos os Professores</h6>
            <p>Diferente do dashboard do professor, aqui você vê as observações mais recentes de <strong>toda a equipe</strong>:</p>
            <ul>
                <li><strong>Professor:</strong> quem criou a observação</li>
                <li><strong>Criança:</strong> aluno observado</li>
                <li><strong>Turma:</strong> turma de origem</li>
                <li><strong>Data:</strong> quando foi registrada</li>
                <li><strong>Status:</strong> rascunho, finalizada ou em revisão</li>
            </ul>
            <p>Use esta tabela para acompanhar a produtividade dos professores e identificar quem precisa de apoio no registro pedagógico.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Menu Dedicado do Coordenador</h6>
            <p>O coordenador tem um menu lateral dedicado à supervisão pedagógica, com acesso direto às suas funções principais:</p>
            <ul>
                <li><strong>Turmas:</strong> visão geral de todas as turmas sob supervisão</li>
                <li><strong>Observações:</strong> revisar observações dos professores; registrar Feedbacks da Coordenação</li>
                <li><strong>Pareceres:</strong> revisar pareceres descritivos; aprovar ou solicitar revisão</li>
                <li><strong>Portfólios:</strong> acompanhar portfólios das turmas; registrar Feedbacks da Coordenação</li>
                <li><strong>Gestão de Professores:</strong> visão geral dos professores, turmas e atividade pedagógica</li>
                <li><strong>Acompanhamento de Cursos:</strong> relatório de progresso nos cursos de formação</li>
            </ul>
            <p>Esse menu dedicado facilita a navegação do coordenador sem misturar com funções administrativas ou de professor.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Card de Visão Geral</h6>
            <p>Ainda na barra lateral, um card de resumo consolida as informações mais relevantes:</p>
            <ul>
                <li><strong>Observações pendentes:</strong> quantas observações finalizadas aguardam sua revisão</li>
                <li><strong>Pareceres para revisar:</strong> pareceres descritivos que precisam de aprovação</li>
                <li><strong>Portfólios enviados:</strong> portfólios de turma finalizados pelos professores</li>
            </ul>
            <p>Este card funciona como sua lista de tarefas: mantenha os números baixos revisando os documentos regularmente.</p>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> Use as notificações (ícone de sino <i class="fas fa-bell"></i>) para ser avisado automaticamente quando um professor finalizar uma observação ou parecer. Assim você não precisa verificar manualmente.
</div>

<div class="help-tip help-tip-success">
    <i class="fas fa-check-circle me-2"></i>
    <strong>Dica:</strong> Ao revisar uma observação, você pode solicitar revisão com uma anotação específica. O professor receberá uma notificação com seu comentário e poderá corrigir o documento.
</div>
