<p class="lead">O Parecer Descritivo é um documento pedagógico individual que registra o desenvolvimento de cada criança ao longo do semestre, reunindo observações dos cinco eixos de atividade em uma narrativa única e personalizada.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Importante:</strong> O Parecer Descritivo é um documento <strong>individual</strong> (por aluno). Para o documento coletivo da turma, utilize o <a href="/admin/help/portfolio/criar-portfolio">Portfólio</a>.
</div>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Acesse a seção de Pareceres Descritivos</h6>
            <p>No menu lateral, navegue até <strong>Pedagógico → Pareceres Descritivos</strong>. A página de listagem será exibida com todos os pareceres já criados.</p>
            <ul>
                <li><strong>Filtros disponíveis:</strong> Filtre por turma, semestre, ano e status (rascunho, finalizado, revisão solicitada)</li>
                <li><strong>Contadores de status:</strong> No topo da listagem são exibidos contadores com a quantidade de pareceres em cada status, dando uma visão rápida do andamento</li>
                <li>Cada card mostra o nome do aluno, turma, semestre e um badge indicando o status atual</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Clique em "Novo Parecer"</h6>
            <p>O botão verde no topo da página abre o formulário de criação. Preencha os campos obrigatórios:</p>
            <ul>
                <li><strong>Aluno:</strong> Selecione o aluno para o qual deseja criar o parecer</li>
                <li><strong>Semestre:</strong> Escolha o semestre (1 ou 2)</li>
                <li><strong>Ano:</strong> Informe o ano letivo de referência</li>
                <li><strong>Observação vinculada (opcional):</strong> Se o aluno já possui uma observação finalizada para o período, você pode vinculá-la ao parecer. Apenas observações com status "finalizado" aparecem na lista</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Compilação automática do texto</h6>
            <p>Ao vincular uma observação finalizada, o sistema realiza automaticamente a compilação dos textos dos 6 eixos de observação em uma narrativa unificada.</p>
            <ul>
                <li>Os textos registrados em cada eixo da observação são reunidos e organizados no campo <strong>"student_text"</strong> (Texto da Criança)</li>
                <li>A narrativa é construída de forma contínua, integrando todos os eixos em um relato coeso sobre o desenvolvimento da criança</li>
                <li>Esse texto compilado pode ser editado livremente depois, sem alterar a observação original</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Parecer criado em modo rascunho</h6>
            <p>O parecer é criado com o status <strong>"Rascunho"</strong> e aparece imediatamente na listagem. A partir daqui, você pode editá-lo para refinar o texto, adicionar fotos e preparar para finalização.</p>
            <ul>
                <li><strong>Texto introdutório:</strong> O parecer já vem com um texto padrão de apresentação pedagógica direcionado às famílias, iniciando com "Queridas famílias...", que pode ser personalizado</li>
                <li>Na listagem, o parecer é identificado pelo nome do aluno, turma, semestre e badge de status</li>
            </ul>
        </div>
    </div>
</div>

<h5 class="mt-4 mb-3"><i class="fas fa-file-alt me-2 text-primary"></i>Estrutura do Parecer Descritivo</h5>
<p>O documento final do Parecer Descritivo é composto por até 7 páginas com a seguinte estrutura:</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number"><i class="fas fa-image"></i></div>
        <div class="step-content">
            <h6>Capa</h6>
            <p>Página de capa com fundo verde institucional, foto do aluno, nome, turma e semestre. A foto é enviada na aba "Capa" durante a edição.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number"><i class="fas fa-school"></i></div>
        <div class="step-content">
            <h6>Página 1 — Texto Institucional</h6>
            <p>Texto fixo institucional com o título "Sobre o Parecer Descritivo", explicando às famílias o propósito e a metodologia do documento pedagógico.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number"><i class="fas fa-child"></i></div>
        <div class="step-content">
            <h6>Página 2 — Narrativa da Criança</h6>
            <p>Texto personalizado com o nome do aluno como título, contendo a narrativa compilada dos eixos de observação (campo "student_text"). Este é o coração do parecer.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number"><i class="fas fa-camera"></i></div>
        <div class="step-content">
            <h6>Páginas 3 a 7 — Eixos com Fotos</h6>
            <p>Uma página para cada um dos 5 eixos de atividade, contendo o título do eixo e até 3 fotos com legendas, dispostas em layout 2+1 (duas fotos lado a lado na parte superior e uma centralizada abaixo).</p>
        </div>
    </div>
</div>

