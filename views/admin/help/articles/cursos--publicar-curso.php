<p class="lead">Depois de montar a estrutura do curso, é hora de configurar quizzes, adicionar materiais de apoio, ativar o curso e acompanhar os alunos.</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Adicionar Quizzes</h6>
            <p>Na página do curso, clique em <strong>"Quizzes"</strong> para acessar o gerenciador de avaliações. Crie quizzes vinculados a seções:</p>
            <ul>
                <li><strong>Título e descrição</strong> — nome e contexto do quiz</li>
                <li><strong>Nota mínima:</strong> Percentual para aprovação (padrão: 70%)</li>
                <li><strong>Tempo limite:</strong> Minutos para completar (opcional)</li>
                <li><strong>Tentativas:</strong> Número máximo (0 = ilimitado, padrão: 3)</li>
            </ul>
            <p>Após criar o quiz, adicione perguntas com respostas de múltipla escolha ou verdadeiro/falso. Cada pergunta pode ter pontuação individual.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Materiais de Apoio</h6>
            <p>Na página do curso, clique em <strong>"Materiais"</strong>. Envie arquivos complementares:</p>
            <ul>
                <li><strong>Formatos aceitos:</strong> PDF, Excel, Word, PowerPoint, imagens, vídeos, ZIP</li>
                <li><strong>Tamanho máximo:</strong> 50MB por arquivo</li>
                <li>Organize por seção ou como material geral do curso</li>
                <li>Os alunos podem baixar pelo painel do curso</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Ativar o Curso</h6>
            <p>Para publicar, edite o curso e certifique-se de que:</p>
            <ul>
                <li><strong>Status "Ativo"</strong> está marcado ✓</li>
                <li>Pelo menos uma lição com vídeo foi cadastrada</li>
                <li>A imagem de capa está definida</li>
            </ul>
            <p>Cursos inativos não aparecem no catálogo público e não podem receber novas matrículas.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Pré-visualizar como Aluno</h6>
            <p>Use o botão <strong>"Ver como Aluno"</strong> na página do curso para acessar a visão pública. Isso abre o player do curso em uma nova aba, permitindo validar como os alunos verão o conteúdo.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Gerenciar após publicação</h6>
            <p>Após ativar o curso, você pode:</p>
            <ul>
                <li><strong>Adicionar conteúdo</strong> — novas lições e materiais a qualquer momento</li>
                <li><strong>Acompanhar matrículas</strong> — via Ensino → Matrículas</li>
                <li><strong>Monitorar progresso</strong> — no Dashboard de Vídeos</li>
                <li><strong>Ver relatórios</strong> — em Análises → Relatórios</li>
                <li><strong>Desativar temporariamente</strong> — desmarcando "Ativo" (matrículas existentes continuam)</li>
            </ul>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Importante:</strong> Um curso com matrículas vinculadas <strong>não pode ser excluído</strong>. Para retirar do catálogo, desative-o. As matrículas existentes continuam funcionando normalmente.
</div>

<div class="help-tip help-tip-success">
    <i class="fas fa-check-circle me-2"></i>
    <strong>Ciclo completo:</strong> Criar curso → Adicionar módulos/seções/lições → Configurar quizzes → Enviar materiais → Ativar → Matricular alunos → Acompanhar progresso.
</div>
