<p class="lead">O Portfólio é um documento pedagógico coletivo que registra as vivências e atividades de toda a turma ao longo do semestre, organizando fotos, descrições e a mensagem da professora em um material rico para as famílias.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Importante:</strong> O Portfólio é um documento <strong>coletivo</strong> (por turma). Para documentos individuais de cada aluno, utilize o <a href="/admin/help/parecer-descritivo/criar-parecer">Parecer Descritivo</a>.
</div>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Acesse a seção de Portfólios</h6>
            <p>No menu lateral, navegue até <strong>Pedagógico → Portfólios</strong>. A página exibe os portfólios já criados em formato de cards:</p>
            <ul>
                <li>Cada card mostra o <strong>nome da turma</strong>, o <strong>semestre</strong> e um <strong>badge de status</strong></li>
                <li>Os status possíveis são: <strong>Rascunho</strong> (cinza), <strong>Finalizado</strong> (verde) e <strong>Revisão Solicitada</strong> (amarelo)</li>
                <li>Use os filtros disponíveis para localizar portfólios por turma, semestre, ano ou status</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Clique em "Novo Portfólio"</h6>
            <p>O botão verde no topo da página abre o formulário de criação. Preencha os campos obrigatórios:</p>
            <ul>
                <li><strong>Turma:</strong> Selecione a turma para a qual deseja criar o portfólio. Cada turma pode ter apenas um portfólio por semestre</li>
                <li><strong>Semestre:</strong> Escolha o semestre (1 ou 2)</li>
                <li><strong>Ano:</strong> Informe o ano letivo de referência</li>
            </ul>
            <p class="text-muted mt-2"><i class="fas fa-exclamation-triangle me-1"></i> A combinação de Turma + Semestre + Ano deve ser <strong>única</strong>. O sistema não permitirá criar duplicatas.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Selecione a foto de capa do Banco de Imagens</h6>
            <p>A foto de capa do portfólio deve ser uma <strong>foto coletiva da turma</strong>, selecionada a partir do Banco de Imagens interno:</p>
            <ul>
                <li>Clique em <strong>"Selecionar do Banco de Imagens"</strong> para abrir o modal de seleção</li>
                <li>Escolha uma foto que represente o grupo de crianças da turma</li>
                <li>Fotos de atividades em grupo, rodas ou momentos de confraternização funcionam muito bem</li>
                <li>A foto será exibida na capa do documento PDF com o fundo verde institucional</li>
                <li><strong>Pré-requisito:</strong> as fotos devem ter sido previamente enviadas ao <a href="/admin/help/banco-imagens/upload-gerenciar">Banco de Imagens</a> da turma</li>
            </ul>

            <div class="help-tip">
                <i class="fas fa-lightbulb me-2"></i>
                <strong>Importante (v4):</strong> O upload direto de fotos foi removido do portfólio. Todas as fotos — capa e eixos — são selecionadas exclusivamente do Banco de Imagens interno da turma. Isso centraliza o acervo fotográfico e facilita a reutilização das fotos.
            </div>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Escreva a mensagem da professora</h6>
            <p>O campo de mensagem é um espaço para a professora se comunicar diretamente com as famílias:</p>
            <ul>
                <li>Escreva uma <strong>saudação</strong> às famílias sobre o semestre que se encerra</li>
                <li>Destaque os <strong>momentos marcantes</strong> e conquistas coletivas da turma</li>
                <li>Compartilhe reflexões sobre o <strong>desenvolvimento do grupo</strong></li>
                <li>Mantenha um tom acolhedor e profissional</li>
                <li>Este texto aparecerá em uma página dedicada no PDF final ("Mensagem da Professora")</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Portfólio criado em modo rascunho</h6>
            <p>Ao salvar, o portfólio é criado com status <strong>"Rascunho"</strong> e aparece na listagem:</p>
            <ul>
                <li>O portfólio pode ser editado livremente enquanto estiver em rascunho</li>
                <li>Você pode voltar a qualquer momento para completar as informações</li>
                <li>Os próximos passos incluem preencher as descrições dos eixos e adicionar fotos</li>
            </ul>
        </div>
    </div>
</div>

