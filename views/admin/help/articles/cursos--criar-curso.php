<p class="lead">Cadastre um novo curso completo com título, descrição, imagem de capa e configurações de acesso.</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Acesse Ensino → Cursos</h6>
            <p>No menu lateral, clique em <strong>Cursos</strong>. Você verá a lista de cursos existentes com suas estatísticas.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Clique em "Novo Curso"</h6>
            <p>Preencha as informações básicas:</p>
            <ul>
                <li><strong>Título:</strong> Nome do curso que aparecerá para os alunos</li>
                <li><strong>Descrição:</strong> Texto explicativo sobre o conteúdo</li>
                <li><strong>Imagem de capa:</strong> Upload de uma imagem representativa</li>
                <li><strong>Instrutor:</strong> Quem será o responsável pelo curso</li>
                <li><strong>Gratuito/Pago:</strong> Defina o modelo de acesso</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Organize o conteúdo</h6>
            <p>Após criar o curso, adicione a estrutura:</p>
            <ul>
                <li><strong>Seções:</strong> Divisões temáticas do curso</li>
                <li><strong>Módulos:</strong> Agrupamentos dentro das seções</li>
                <li><strong>Lições:</strong> Vídeos e conteúdo dentro dos módulos</li>
                <li><strong>Quizzes:</strong> Avaliações para verificar o aprendizado</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Ative o curso</h6>
            <p>Quando o conteúdo estiver pronto, ative o curso para que os alunos possam acessá-lo. Cursos inativos não aparecem no catálogo.</p>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> Adicione materiais de apoio (PDFs, slides) ao curso para complementar as videoaulas. Acesse a aba <strong>Materiais</strong> dentro da página do curso.
</div>

