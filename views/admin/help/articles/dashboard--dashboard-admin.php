<p class="lead">O dashboard do administrador é o painel mais completo da plataforma. Ele reúne métricas de todos os módulos, permitindo uma visão 360° da operação educacional — desde matrículas e cursos até observações pedagógicas e engajamento dos alunos.</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>8 Cards de Métricas no Topo</h6>
            <p>Os cards no topo oferecem um resumo instantâneo de toda a plataforma:</p>
            <ul>
                <li><strong>Total Alunos:</strong> quantidade total de alunos cadastrados, com indicador de novos alunos registrados no mês atual</li>
                <li><strong>Matrículas Ativas:</strong> total de matrículas com status ativo, incluindo contagem de matrículas já concluídas</li>
                <li><strong>Cursos Ativos:</strong> número de cursos publicados e disponíveis, com progresso médio percentual de todos os matriculados</li>
                <li><strong>Contatos:</strong> mensagens recebidas pelo formulário de contato do site, com indicador de mensagens não lidas</li>
                <li><strong>Escolas:</strong> quantidade de escolas ativas e inativas no sistema</li>
                <li><strong>Horas Assistidas:</strong> total acumulado de horas de vídeo assistidas por todos os alunos</li>
                <li><strong>Quiz Attempts:</strong> número total de tentativas de quizzes realizadas pelos alunos</li>
                <li><strong>Observações:</strong> total de observações pedagógicas registradas na plataforma</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Matrículas Recentes</h6>
            <p>Tabela com as 8 matrículas mais recentes realizadas na plataforma:</p>
            <ul>
                <li><strong>Aluno:</strong> nome do aluno matriculado</li>
                <li><strong>Curso:</strong> em qual curso foi matriculado</li>
                <li><strong>Data:</strong> quando a matrícula foi realizada</li>
                <li><strong>Progresso:</strong> percentual de conclusão atual</li>
                <li><strong>Status:</strong> ativo, concluído, cancelado ou pendente</li>
            </ul>
            <p>Acompanhe o fluxo de novas matrículas e identifique rapidamente alunos que acabaram de ingressar.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Cursos Populares</h6>
            <p>Ranking dos 5 cursos com maior número de matrículas:</p>
            <ul>
                <li><strong>Nome do curso:</strong> título do curso</li>
                <li><strong>Total de matrículas:</strong> quantos alunos estão matriculados</li>
                <li><strong>Progresso médio:</strong> média de conclusão dos matriculados</li>
            </ul>
            <p>Use esta informação para identificar os cursos mais procurados e planejar novos conteúdos na mesma linha.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Alunos com Baixo Progresso</h6>
            <p>Lista de alunos que precisam de atenção especial:</p>
            <ul>
                <li><strong>Critério:</strong> alunos com menos de 20% de progresso após 7 ou mais dias desde a matrícula</li>
                <li><strong>Aluno:</strong> nome do estudante</li>
                <li><strong>Curso:</strong> curso com baixo desempenho</li>
                <li><strong>Progresso:</strong> percentual atual de conclusão</li>
                <li><strong>Dias matriculado:</strong> há quantos dias está matriculado</li>
            </ul>
            <p>Esta seção ajuda a identificar alunos que podem estar com dificuldades ou que abandonaram o curso e precisam de contato.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Contatos Recentes e Observações</h6>
            <p>Duas seções adicionais completam a visão geral:</p>
            <ul>
                <li><strong>Contatos recentes:</strong> últimas mensagens recebidas pelo formulário do site, com nome, assunto e data. Mensagens não lidas ficam destacadas.</li>
                <li><strong>Observações recentes:</strong> últimas observações pedagógicas registradas por todos os professores, com nome da criança, professor, turma e status.</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">6</div>
        <div class="step-content">
            <h6>Ações Rápidas na Barra Lateral</h6>
            <p>A barra lateral direita do admin oferece atalhos para todas as operações do sistema:</p>
            <ul>
                <li><strong>Novo Aluno:</strong> cadastrar uma criança rapidamente</li>
                <li><strong>Nova Matrícula:</strong> matricular um aluno em um curso</li>
                <li><strong>Novo Curso:</strong> criar um curso de formação</li>
                <li><strong>Gerenciar Turmas:</strong> acessar a gestão de turmas</li>
                <li><strong>Relatórios:</strong> ir direto aos relatórios analíticos</li>
                <li><strong>Usuários:</strong> gerenciar contas de acesso</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">7</div>
        <div class="step-content">
            <h6>Cards Complementares na Barra Lateral</h6>
            <p>Além das ações rápidas, a barra lateral exibe cards informativos:</p>
            <ul>
                <li><strong>Resumo de Quizzes:</strong> total de tentativas, taxa de aprovação e nota média geral dos quizzes</li>
                <li><strong>Status das Escolas:</strong> lista de escolas com indicador visual de status (ativa em verde, inativa em vermelho) e datas do contrato</li>
            </ul>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> Os dados do dashboard são atualizados em tempo real a cada vez que a página é carregada. Pressione <kbd>F5</kbd> ou clique em "Dashboard" no menu lateral para atualizar as métricas.
</div>

<div class="help-tip help-tip-success">
    <i class="fas fa-check-circle me-2"></i>
    <strong>Dica:</strong> Fique atento ao card de "Alunos com Baixo Progresso". Alunos inativos por mais de 7 dias com menos de 20% de progresso podem precisar de um contato para reengajamento ou suporte técnico.
</div>
