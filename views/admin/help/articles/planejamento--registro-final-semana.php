<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Acesse e preencha o formulário</h6>
            <p>Abra o Registro Final da Semana do planejamento desejado. Preencha os campos conforme a vivência da semana:</p>
            <ul>
                <li>Cada campo é um <strong>textarea</strong> de texto livre — escreva com suas próprias palavras</li>
                <li>Não há resposta certa ou errada — o objetivo é a reflexão pedagógica honesta</li>
                <li>Você pode preencher os campos em qualquer ordem</li>
                <li>Use o campo <strong>"Necessidade de Apoio"</strong> para comunicar à coordenação situações que merecem atenção</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Salve o registro</h6>
            <p>Após preencher os campos, clique em <strong>"Salvar Registro"</strong> para gravar as informações:</p>
            <ul>
                <li>O registro é salvo diretamente vinculado ao planejamento da semana</li>
                <li>Você pode salvar e voltar para continuar editando depois, se necessário</li>
                <li>Ao finalizar, o badge na listagem muda de <span class="badge bg-warning text-dark">Pendente</span> para <span class="badge bg-success">Concluído</span></li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Visualização pelo coordenador</h6>
            <p>O coordenador pode visualizar os Registros Finais da Semana de todos os professores das escolas sob sua supervisão:</p>
            <ul>
                <li>Acesse a listagem de planejamentos — a coluna "Registros do Período" mostra o status de cada registro</li>
                <li>Clique no badge para ler o registro completo</li>
                <li>Use o campo <strong>"Necessidade de Apoio"</strong> como indicativo de quais professores precisam de acompanhamento mais próximo</li>
            </ul>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> Preencha o Registro Final da Semana na sexta-feira, ao final do dia, ou no início da semana seguinte. As memórias ainda estarão frescas e o registro será mais rico e preciso.
</div>

