<p class="lead">O Registro Final da Semana é um formulário dedicado, vinculado a cada planejamento, onde o professor reflete sobre o que foi vivenciado durante a semana — preenchido após a execução das atividades.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Diferença importante:</strong> O <strong>Registro Final da Semana</strong> é específico de cada planejamento e foca na síntese da semana como um todo. Ele é diferente do <strong>Registro Pós-Vivência</strong> (que abrange o período quinzenal completo e pertence ao fluxo de status do planejamento).
</div>

<h6 class="fw-bold mt-4 mb-3">Como acessar o Registro Final da Semana</h6>

<p>O registro é acessado diretamente pela listagem de planejamentos ou pela página de um planejamento específico:</p>
<ul>
    <li><strong>Pela listagem:</strong> na coluna <strong>"Registros do Período"</strong>, o badge indica se o registro está Concluído (verde) ou Pendente (laranja). Clique no badge para acessar o formulário</li>
    <li><strong>Pelo planejamento:</strong> abra o planejamento desejado e clique no botão <strong>"Registro Final da Semana"</strong> disponível na página de detalhes</li>
</ul>

