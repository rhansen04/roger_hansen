<p class="lead">Crie planejamentos pedagógicos completos usando templates prontos ou começando do zero.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Você sabia?</strong> Você pode criar templates personalizados em <strong>Ensino → Templates Planej.</strong> para reutilizar estruturas de planejamento.
</div>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Acesse a seção de Planejamentos</h6>
            <p>No menu lateral, clique em <strong>Ensino → Planejamentos</strong>. Você verá a lista de planejamentos existentes.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Clique em "Novo Planejamento"</h6>
            <p>O botão verde no topo da página abre o formulário de criação. Você escolherá:</p>
            <ul>
                <li><strong>Título:</strong> Nome descritivo do planejamento</li>
                <li><strong>Template:</strong> Selecione um template para pré-preencher as seções</li>
                <li><strong>Escola e Turma:</strong> Vincule a um contexto escolar (opcional)</li>
                <li><strong>Período:</strong> Datas de início e fim</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Preencha as seções do planejamento</h6>
            <p>Cada template define seções com campos específicos. Preencha os campos de texto, seleções e checkboxes conforme necessário.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Salve como rascunho ou envie para revisão</h6>
            <p>Você pode salvar o planejamento a qualquer momento. Use os botões de ação para mudar o status:</p>
            <ul>
                <li><strong>Rascunho:</strong> Ainda em construção, só você vê</li>
                <li><strong>Em Revisão:</strong> Enviado para aprovação</li>
                <li><strong>Aprovado:</strong> Pronto para execução</li>
            </ul>
        </div>
    </div>
</div>

