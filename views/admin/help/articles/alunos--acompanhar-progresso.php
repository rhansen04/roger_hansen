<p class="lead">Monitore o progresso dos alunos em tempo real: vídeos assistidos, quizzes realizados, certificados emitidos e muito mais.</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Barra de progresso nas Matrículas</h6>
            <p>Em <strong>Ensino → Matrículas</strong>, cada matrícula mostra uma <strong>barra de progresso</strong> visual com a porcentagem de conclusão. Use os filtros para encontrar alunos específicos ou ver apenas matrículas concluídas.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Dashboard de Vídeos</h6>
            <p>Para uma visão detalhada do engajamento, acesse <strong>Análises → Vídeos / Tracking</strong>. O dashboard oferece:</p>
            <ul>
                <li><strong>Cards de resumo:</strong> Matrículas ativas, vídeos completados, sessões, horas assistidas</li>
                <li><strong>Progresso por curso:</strong> Tabela com média de progresso e conclusões</li>
                <li><strong>Ranking de alunos:</strong> Os mais ativos com progresso e última atividade</li>
                <li><strong>Atividade recente:</strong> Sessões dos últimos 7 dias com quantidade de minutos</li>
                <li><strong>Alunos inativos:</strong> Quem não acessou há mais de 7 dias (alerta vermelho)</li>
                <li><strong>Sessões recentes:</strong> Últimas 20 sessões com detalhes de IP, duração e progresso</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Detalhe por aluno</h6>
            <p>Clique no nome de um aluno no Dashboard de Vídeos para ver o <strong>perfil de engajamento individual</strong>:</p>
            <ul>
                <li><strong>Progresso geral:</strong> Percentual e contagem de vídeos (X/Y)</li>
                <li><strong>Status por lição:</strong> Concluído, Em andamento ou Não iniciado</li>
                <li><strong>Progresso do vídeo:</strong> Barra com tempo assistido (MM:SS / MM:SS)</li>
                <li><strong>Número de sessões:</strong> Quantas vezes acessou cada lição</li>
                <li><strong>Histórico de sessões:</strong> Últimas 50 sessões com início, fim, duração, progresso antes/depois, dispositivo e IP</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Conclusão e Certificados</h6>
            <p>Quando um aluno completa 100% do curso:</p>
            <ul>
                <li>O sistema marca a matrícula como <strong>"Concluída"</strong></li>
                <li>O aluno pode gerar um <strong>certificado digital</strong> com código de verificação único</li>
                <li>O certificado pode ser verificado publicamente pela URL <code>/certificado/{código}</code></li>
                <li>A informação aparece no Dashboard e nos Relatórios</li>
            </ul>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> Fique atento à seção <strong>"Alunos Inativos"</strong> no Dashboard de Vídeos. Ela mostra alunos que não acessaram o curso há mais de 7 dias — uma boa oportunidade para enviar lembretes.
</div>

