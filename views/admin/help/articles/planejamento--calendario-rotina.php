<p class="lead">O Calendário de Planejamentos oferece uma visão mensal das semanas letivas com seus respectivos planejamentos, enquanto a Rotina Semanal permite detalhar as atividades diárias de segunda a sexta-feira.</p>

<h5 class="mt-4 mb-3"><i class="fas fa-calendar-alt me-2 text-primary"></i>Calendário de Planejamentos</h5>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Acesse o calendário</h6>
            <p>Existem duas formas de acessar o calendário de planejamentos:</p>
            <ul>
                <li><strong>Menu lateral:</strong> Navegue até <strong>Planejamentos → Calendário</strong></li>
                <li><strong>Botão na listagem:</strong> Na página de listagem de planejamentos, clique no botão <strong>"Calendário"</strong> no topo da página</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Navegue pelo calendário mensal</h6>
            <p>O calendário exibe uma visualização mensal com seletores para navegar entre períodos:</p>
            <ul>
                <li><strong>Seletor de ano:</strong> alterne entre anos letivos para consultar planejamentos de períodos anteriores</li>
                <li><strong>Seletor de mês:</strong> navegue mês a mês para visualizar as semanas desejadas</li>
                <li><strong>Filtro de turma:</strong> disponível para <strong>Administradores</strong> e <strong>Coordenadores</strong>, permite filtrar os planejamentos por turma específica. Professores veem automaticamente apenas suas turmas</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Visualize as semanas e seus planejamentos</h6>
            <p>O calendário exibe as semanas do mês de segunda a sexta-feira (dias letivos). Para cada semana são mostrados:</p>
            <ul>
                <li><strong>Intervalo de datas:</strong> data de início (segunda) e fim (sexta) da semana</li>
                <li><strong>Planejamentos vinculados:</strong> se houver um planejamento submetido para aquela semana, ele aparece como um link clicável com o título e um badge de status</li>
                <li><strong>Badges de status:</strong> indicadores coloridos mostrando o estado atual do planejamento — <span class="badge bg-secondary">Rascunho</span>, <span class="badge bg-primary">Finalizado</span>, <span class="badge bg-warning text-dark">Revisão Solicitada</span></li>
                <li><strong>Semanas sem planejamento:</strong> exibidas com indicação visual de que ainda não há submissão para o período</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Crie um novo planejamento a partir do calendário</h6>
            <p>Em cada semana que ainda não possui planejamento, um botão <strong>"+"</strong> permite criar uma nova submissão:</p>
            <ul>
                <li>Ao clicar no "+", o formulário de criação é aberto com as <strong>datas de início e fim da semana já preenchidas</strong> automaticamente</li>
                <li>Isso agiliza o processo, eliminando a necessidade de digitar manualmente o período</li>
                <li>Os demais campos do planejamento devem ser preenchidos normalmente conforme o template</li>
            </ul>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> Use o calendário para ter uma visão geral de quais semanas já possuem planejamento e quais ainda estão pendentes. Isso é especialmente útil para coordenadores acompanharem a entrega dos professores.
</div>

<h5 class="mt-4 mb-3"><i class="fas fa-clock me-2 text-primary"></i>Rotina Semanal</h5>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Acesse a rotina semanal</h6>
            <p>A rotina semanal é acessada a partir de um planejamento específico. Abra o planejamento desejado (página de visualização) e clique no botão <strong>"Rotina Semanal"</strong>.</p>
            <ul>
                <li>A rotina está vinculada ao planejamento da semana, detalhando as atividades dia a dia</li>
                <li>Se o planejamento estiver com status <strong>"Registrado"</strong> (finalizado e aprovado), a rotina será exibida em <strong>modo somente leitura</strong></li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Visualize a estrutura de 5 colunas</h6>
            <p>A rotina é organizada em <strong>5 colunas</strong>, uma para cada dia útil da semana:</p>
            <ul>
                <li><strong>Segunda-feira</strong></li>
                <li><strong>Terça-feira</strong></li>
                <li><strong>Quarta-feira</strong></li>
                <li><strong>Quinta-feira</strong></li>
                <li><strong>Sexta-feira</strong></li>
            </ul>
            <p>Cada coluna exibe a lista de atividades planejadas para aquele dia, ordenadas por <strong>sort_order</strong> e <strong>horário</strong>.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Adicione atividades à rotina</h6>
            <p>Em cada coluna (dia da semana), clique no botão <strong>"+ Adicionar atividade"</strong> para inserir uma nova atividade. Para cada atividade, preencha:</p>
            <ul>
                <li><strong>Horário:</strong> campo de texto livre para indicar a faixa horária (ex: <code>08:00-08:30</code>, <code>09:00-09:45</code>)</li>
                <li><strong>Descrição:</strong> texto descrevendo a atividade planejada para aquele horário (ex: "Roda de conversa", "Atividade musical com instrumentos", "Parque")</li>
                <li>Você pode adicionar quantas atividades forem necessárias para cada dia</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Remova atividades</h6>
            <p>Para remover uma atividade, clique no botão <strong>"X"</strong> (ícone de fechar) ao lado da atividade desejada. A atividade é removida da lista imediatamente na interface.</p>
            <ul>
                <li>A remoção só é efetivada no banco de dados ao clicar em "Salvar Rotina"</li>
                <li>Até salvar, você pode desfazer a remoção recarregando a página</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Salve a rotina completa</h6>
            <p>Após organizar todas as atividades da semana, clique no botão <strong>"Salvar Rotina"</strong> no final da página. O sistema realiza a gravação em lote:</p>
            <ul>
                <li>Todas as atividades dos 5 dias são enviadas de uma vez via <strong>POST</strong></li>
                <li>O sistema utiliza uma <strong>transação</strong> para garantir a integridade: primeiro remove todas as atividades anteriores e depois insere as novas (delete + reinsert)</li>
                <li>As atividades são ordenadas pelo campo <strong>sort_order</strong> e pelo <strong>time_slot</strong> (horário)</li>
                <li>Uma mensagem de sucesso confirma que a rotina foi salva</li>
            </ul>
        </div>
    </div>
</div>

<div class="help-tip help-tip-success">
    <i class="fas fa-check-circle me-2"></i>
    <strong>Dica:</strong> Monte a rotina semanal logo após criar o planejamento. Assim, o coordenador poderá avaliar tanto o planejamento pedagógico quanto a organização prática das atividades no dia a dia.
</div>
