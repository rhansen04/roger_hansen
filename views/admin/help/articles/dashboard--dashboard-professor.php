<p class="lead">O dashboard do professor é sua central de trabalho. Ele reúne informações sobre suas turmas, alunos, cursos e tarefas pendentes em uma única tela, permitindo acesso rápido a tudo que você precisa no dia a dia.</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Cards de Resumo no Topo</h6>
            <p>Ao acessar o dashboard, você encontra 4 cards com as métricas mais importantes:</p>
            <ul>
                <li><strong>Turmas Ativas:</strong> quantidade de turmas ativas vinculadas a você no ano letivo atual. Clique para ir direto à lista de turmas.</li>
                <li><strong>Meus Alunos:</strong> total de crianças matriculadas nas suas turmas ativas. Mostra o universo de alunos sob sua responsabilidade.</li>
                <li><strong>Meus Cursos:</strong> número de cursos de formação em que você está matriculado. Acompanhe sua própria capacitação.</li>
                <li><strong>Pareceres Pendentes:</strong> quantidade de pareceres descritivos que ainda não foram finalizados. Use este card como lembrete para completar os pareceres no prazo.</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Tabela de Cursos com Progresso</h6>
            <p>Na área principal, a tabela de cursos mostra os cursos de formação em que você está matriculado:</p>
            <ul>
                <li><strong>Nome do curso:</strong> título do curso de formação continuada</li>
                <li><strong>Barra de progresso:</strong> indicador visual colorido mostrando seu percentual de conclusão (0-100%)</li>
                <li><strong>Status:</strong> ativo, concluído ou pendente</li>
            </ul>
            <p>Clique no nome do curso para acessá-lo diretamente e continuar seus estudos.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Observações Recentes</h6>
            <p>Logo abaixo, uma tabela mostra as 5 últimas observações pedagógicas que você criou:</p>
            <ul>
                <li><strong>Criança:</strong> nome do aluno observado</li>
                <li><strong>Turma:</strong> turma à qual pertence</li>
                <li><strong>Data:</strong> quando a observação foi registrada</li>
                <li><strong>Status:</strong> rascunho, finalizada ou em revisão</li>
            </ul>
            <p>Essa visão rápida permite retomar observações em andamento ou verificar as mais recentes sem precisar navegar até o módulo completo.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Barra Lateral Direita — Minhas Turmas</h6>
            <p>Na lateral direita do dashboard, você encontra a lista das suas turmas com informações resumidas:</p>
            <ul>
                <li><strong>Nome da turma:</strong> identificação da sala</li>
                <li><strong>Faixa etária:</strong> idade das crianças (ex: 4-5 anos)</li>
                <li><strong>Escola:</strong> a qual escola pertence</li>
                <li><strong>Período:</strong> manhã, tarde ou integral</li>
                <li><strong>Ano letivo:</strong> ano de referência da turma</li>
            </ul>
            <p>Clique em qualquer turma para acessar seus detalhes, alunos vinculados e documentos pedagógicos.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Ações Rápidas</h6>
            <p>Ainda na barra lateral, os botões de ações rápidas oferecem atalhos para as tarefas mais comuns:</p>
            <ul>
                <li><strong>Nova Observação:</strong> inicia o registro de uma nova observação pedagógica</li>
                <li><strong>Minhas Turmas:</strong> acessa a lista completa das suas turmas</li>
                <li><strong>Meus Cursos:</strong> vai para seus cursos de formação</li>
                <li><strong>Crianças:</strong> acessa a lista de alunos das suas turmas</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">6</div>
        <div class="step-content">
            <h6>Card de Resumo de Observações</h6>
            <p>Na barra lateral, um card compacto resume o status das suas observações pedagógicas:</p>
            <ul>
                <li><strong>Total de observações:</strong> quantas observações você criou no período</li>
                <li><strong>Finalizadas:</strong> observações concluídas e enviadas para revisão</li>
                <li><strong>Em rascunho:</strong> observações iniciadas que ainda precisam ser completadas</li>
            </ul>
            <p>Use este resumo para acompanhar seu progresso no registro pedagógico e identificar pendências.</p>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> Clique no botão <strong>?</strong> na barra superior para iniciar o tour interativo do dashboard. Cada elemento será destacado com uma explicação detalhada da sua função.
</div>

<div class="help-tip help-tip-success">
    <i class="fas fa-check-circle me-2"></i>
    <strong>Dica:</strong> Mantenha o card de "Pareceres Pendentes" sempre em zero! Finalize os pareceres antes do prazo para que o coordenador possa revisá-los com tranquilidade.
</div>
