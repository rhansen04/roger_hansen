<p class="lead">O Registro Pós-Vivência é a etapa final do ciclo de planejamento pedagógico. Após vivenciar as atividades planejadas com as crianças, o professor retorna à plataforma para registrar reflexões, sínteses e avaliações sobre como o planejamento se concretizou na prática.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Pré-requisito:</strong> O Registro Pós-Vivência só fica disponível após o professor clicar em <strong>"Finalizar Planejamento"</strong>, mudando o status para <span class="badge bg-primary">Enviado</span>. Enquanto o planejamento estiver em <span class="badge bg-warning text-dark">Rascunho</span>, este botão não aparece.
</div>

<h6 class="fw-bold mt-4 mb-3">O que é o Registro Pós-Vivência?</h6>
<p>O Registro Pós-Vivência (recurso R2-08) é um formulário complementar que contém apenas as seções marcadas como <code>is_registration=1</code> no template de planejamento. Diferente dos cards diários, o registro pós-vivência se aplica ao <strong>período inteiro</strong> (a quinzena completa), e não a dias individuais.</p>
<p>Exemplos comuns de campos de registro pós-vivência:</p>
<ul>
    <li><strong>Síntese do Desenvolvimento:</strong> como as crianças responderam às atividades ao longo do período</li>
    <li><strong>Execução do Planejamento:</strong> o que foi executado conforme planejado e o que precisou ser adaptado</li>
    <li><strong>Reflexões Pedagógicas:</strong> aprendizados e insights para os próximos planejamentos</li>
    <li><strong>Observações Gerais:</strong> qualquer anotação relevante sobre o período</li>
</ul>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Como acessar o Registro Pós-Vivência</h6>
            <p>Após finalizar o planejamento (status <span class="badge bg-primary">Enviado</span>), acesse a página de dias do planejamento. Na parte inferior da grade de dias, o botão <strong>"Registro Pós-Vivência"</strong> estará visível.</p>
            <ul>
                <li>Acesse <strong>Ensino → Planejamentos</strong></li>
                <li>Clique no planejamento desejado (que já esteja com status Enviado)</li>
                <li>Na grade de dias, localize o botão <strong>"Registro Pós-Vivência"</strong> na parte inferior</li>
                <li>Clique para abrir o formulário de registro</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Seções exibidas no registro</h6>
            <p>O formulário de Registro Pós-Vivência exibe <strong>apenas</strong> as seções do template que foram marcadas como seções de registro (<code>is_registration=1</code>). Cada seção exibe um badge <span class="badge bg-info">Registro Pós-Vivência</span> no cabeçalho para identificação visual.</p>
            <p>Os campos dentro dessas seções podem ser de qualquer tipo suportado pelo template:</p>
            <ul>
                <li><strong>text:</strong> campo de texto simples</li>
                <li><strong>textarea:</strong> área de texto para respostas mais longas</li>
                <li><strong>radio:</strong> seleção única entre opções</li>
                <li><strong>checkbox:</strong> múltipla seleção</li>
                <li><strong>select:</strong> dropdown com opções</li>
                <li><strong>checklist:</strong> lista de verificação</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Registro abrange o período inteiro</h6>
            <p>Diferente dos cards diários (que armazenam dados por dia), o Registro Pós-Vivência armazena dados para o <strong>período completo</strong> da quinzena. Há apenas um registro pós-vivência por planejamento, cobrindo todas as duas semanas.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Salvar Rascunho</h6>
            <p>O botão <strong>"Salvar Rascunho"</strong> permite salvar o progresso do registro sem alterar o status do planejamento:</p>
            <ul>
                <li>O status permanece como <span class="badge bg-primary">Enviado</span></li>
                <li>Os dados preenchidos são salvos e podem ser editados depois</li>
                <li>Você pode salvar quantas vezes quiser antes de finalizar</li>
                <li>Ideal para quando você precisa preencher em etapas ao longo de vários dias</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Finalizar Registro</h6>
            <p>O botão <strong>"Finalizar Registro"</strong> conclui definitivamente o ciclo do planejamento:</p>
            <ul>
                <li>O status do planejamento muda para <span class="badge bg-success">Registrado</span></li>
                <li>Todos os campos do registro se tornam <strong>somente leitura</strong></li>
                <li>Os cards diários também ficam não clicáveis</li>
                <li>Este é o <strong>estado final</strong> — não pode ser revertido pelo professor</li>
                <li>O planejamento fica como documentação permanente no acervo pedagógico</li>
            </ul>
        </div>
    </div>
</div>

