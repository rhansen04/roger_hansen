<p class="lead">O sistema rastreia automaticamente cada segundo de vídeo assistido. Saiba como interpretar os dados e usar o Dashboard de Vídeos.</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Como funciona o rastreamento</h6>
            <p>O tracking de vídeo opera automaticamente em segundo plano quando o aluno assiste uma lição:</p>
            <ul>
                <li><strong>Início de sessão:</strong> Registrado ao dar play no vídeo</li>
                <li><strong>Progresso:</strong> Atualizado periodicamente durante a reprodução</li>
                <li><strong>Fim de sessão:</strong> Registrado ao pausar, sair ou completar</li>
                <li><strong>Dados capturados:</strong> Tempo assistido, posição do vídeo, % antes e depois, IP, dispositivo</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Dashboard de Vídeos — Visão Geral</h6>
            <p>Acesse em <strong>Análises → Vídeos / Tracking</strong>. Os 6 cards no topo mostram:</p>
            <ul>
                <li><strong>Matrículas Ativas:</strong> Total de alunos com acesso</li>
                <li><strong>Vídeos Completados:</strong> Total de lições finalizadas</li>
                <li><strong>Sessões de Vídeo:</strong> Quantas vezes alunos deram play</li>
                <li><strong>Horas Assistidas:</strong> Total acumulado de tempo de vídeo</li>
                <li><strong>Cursos 100%:</strong> Matrículas completamente concluídas</li>
                <li><strong>Progresso Médio:</strong> Média geral de conclusão</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Tabelas de análise</h6>
            <p>O dashboard organiza 5 tabelas de dados:</p>
            <ul>
                <li><strong>Progresso por Curso:</strong> Média de progresso e conclusões por curso, com contagem de lições</li>
                <li><strong>Ranking de Alunos:</strong> Os mais engajados com progresso, vídeos assistidos e última atividade</li>
                <li><strong>Atividade dos Últimos 7 Dias:</strong> Sessões diárias, alunos ativos, minutos e conclusões</li>
                <li><strong>Alunos Inativos (+7 dias):</strong> Quem não acessou recentemente — badge vermelho com dias de inatividade</li>
                <li><strong>Sessões Recentes:</strong> Últimas 20 sessões com aluno, lição, duração, progresso, IP</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Análise individual do aluno</h6>
            <p>Clique no nome de um aluno no ranking para ver o perfil detalhado:</p>
            <ul>
                <li><strong>Cards de resumo:</strong> Email, curso, progresso total, vídeos X/Y, status</li>
                <li><strong>Progresso por lição:</strong> Barra de progresso individual com tempo assistido (MM:SS / MM:SS), status por lição (Concluído/Em andamento/Não iniciado)</li>
                <li><strong>Histórico de sessões:</strong> Últimas 50 sessões com dados completos: início, fim, duração, progresso antes/depois, dispositivo e IP</li>
            </ul>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Indicadores de atenção:</strong>
    <ul class="mt-2 mb-0">
        <li><strong>Sessões muito curtas</strong> (< 1 min) — aluno pode estar com dificuldade técnica</li>
        <li><strong>Muitas sessões na mesma lição</strong> — pode indicar conteúdo difícil</li>
        <li><strong>Inatividade prolongada</strong> — considere enviar um lembrete ao aluno</li>
        <li><strong>Progresso estagnado</strong> — aluno pode ter desistido ou estar com dúvidas</li>
    </ul>
</div>
