<p class="lead">Turmas organizam alunos em grupos vinculados a uma escola, professor e período letivo. Veja como criar e configurar.</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Acesse a gestão de Turmas</h6>
            <p>No menu lateral, clique em <strong>Ensino → Turmas</strong>. Você verá a lista de turmas com as seguintes informações:</p>
            <ul>
                <li><strong>Nome</strong> — identificação da turma</li>
                <li><strong>Escola</strong> — instituição vinculada</li>
                <li><strong>Professor</strong> — docente responsável</li>
                <li><strong>Faixa etária</strong> — badge com o grupo de idade</li>
                <li><strong>Período</strong> — Manhã, Tarde ou Integral</li>
                <li><strong>Ano letivo</strong> — ano de referência</li>
                <li><strong>Status</strong> — Ativa (verde) ou Inativa (cinza)</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Criar nova Turma</h6>
            <p>Clique em <strong>"Nova Turma"</strong> e preencha:</p>
            <ul>
                <li><strong>Nome:</strong> Identificação clara (ex: "Berçário I - Manhã 2026")</li>
                <li><strong>Escola:</strong> Selecione na lista de escolas cadastradas</li>
                <li><strong>Professor:</strong> Selecione o docente responsável</li>
                <li><strong>Faixa etária:</strong> Grupo de idade dos alunos</li>
                <li><strong>Período:</strong> Manhã, Tarde ou Integral</li>
                <li><strong>Ano letivo:</strong> Ano de referência (ex: 2026)</li>
                <li><strong>Status:</strong> Ativa ou Inativa</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Vincular Turmas a Planejamentos</h6>
            <p>Turmas são utilizadas no sistema de <strong>Planejamento Pedagógico</strong>. Ao criar um planejamento, o professor seleciona a turma correspondente, vinculando o plano ao contexto escolar correto.</p>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Pré-requisitos:</strong> Antes de criar turmas, cadastre pelo menos uma <a href="/admin/schools" style="color:var(--primary-color);font-weight:600">escola</a> e um <a href="/admin/users" style="color:var(--primary-color);font-weight:600">usuário professor</a>.
</div>

