<p class="lead">Cada parecer descritivo conta com páginas dedicadas aos cinco eixos de atividade, onde você pode adicionar até 3 fotos com legendas por eixo, ilustrando momentos significativos do desenvolvimento da criança.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> Fotos com boa iluminação e que mostrem a criança em atividade tornam o parecer muito mais rico e significativo para as famílias.
</div>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Acesse a aba "Fotos dos Eixos"</h6>
            <p>Na página de edição do parecer, clique na aba <strong>"Fotos dos Eixos"</strong>. Você verá 5 seções, uma para cada eixo de atividade da Pedagogia Florença:</p>
            <ul>
                <li><strong>1. Atividade Musical</strong> — Momentos de vivência musical, cantigas, instrumentos</li>
                <li><strong>2. Atividade Manual</strong> — Trabalhos artísticos, pintura, modelagem, recorte</li>
                <li><strong>3. Atividade de Contos</strong> — Contação de histórias, teatro, dramatizações</li>
                <li><strong>4. Atividade de Movimento</strong> — Brincadeiras corporais, dança, jogos motores</li>
                <li><strong>5. Programa Comunicação Ativa (PCA)</strong> — Atividades de linguagem e comunicação</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Preencha os campos de foto</h6>
            <p>Cada eixo possui <strong>3 slots de foto</strong>. Para cada slot, preencha:</p>
            <ul>
                <li><strong>URL da Foto:</strong> Insira o endereço da imagem. Você pode:
                    <ul>
                        <li>Usar uma foto do <strong>Banco de Imagens</strong> da plataforma (copiando a URL)</li>
                        <li>Inserir uma <strong>URL externa</strong> de uma imagem hospedada em outro serviço</li>
                    </ul>
                </li>
                <li><strong>Legenda:</strong> Escreva uma breve descrição da foto que será exibida logo abaixo da imagem no PDF. Exemplo: "Maria durante a roda de cantigas", "Pintura com guache sobre papel craft"</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Organize as fotos por eixo</h6>
            <p>Selecione fotos que representem bem as atividades realizadas em cada eixo durante o semestre:</p>
            <ul>
                <li>Priorize fotos que mostrem a <strong>criança em ação</strong>, participando das atividades</li>
                <li>Varie os momentos: início da atividade, desenvolvimento e resultado final</li>
                <li>Não é obrigatório preencher todos os 3 slots — use quantas fotos forem adequadas</li>
                <li>As legendas devem ser curtas e descritivas, contextualizando o momento fotografado</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Salve e visualize o resultado</h6>
            <p>Após preencher as fotos e legendas desejadas, salve as alterações:</p>
            <ul>
                <li>Clique em <strong>"Salvar"</strong> para gravar todas as fotos e legendas de todos os eixos</li>
                <li>Na página de visualização (show), as fotos são exibidas em um <strong>grid organizado por eixo</strong></li>
                <li>Cada eixo aparece como uma seção com seu título e as respectivas fotos em sequência</li>
                <li>Verifique se todas as URLs estão funcionando corretamente — fotos com links quebrados não aparecerão no PDF</li>
            </ul>
        </div>
    </div>
</div>

<h5 class="mt-4 mb-3"><i class="fas fa-file-pdf me-2 text-danger"></i>Como as fotos aparecem no PDF</h5>
<p>No documento PDF final, cada eixo ocupa uma página inteira (páginas 3 a 7) com a seguinte disposição:</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number"><i class="fas fa-heading"></i></div>
        <div class="step-content">
            <h6>Título do Eixo</h6>
            <p>O nome do eixo de atividade é exibido no topo da página com destaque visual (barra verde lateral).</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number"><i class="fas fa-th-large"></i></div>
        <div class="step-content">
            <h6>Layout 2+1 das Fotos</h6>
            <p>As fotos são dispostas em um layout <strong>2+1</strong>:</p>
            <ul>
                <li><strong>Linha superior:</strong> Duas fotos lado a lado, cada uma ocupando metade da largura</li>
                <li><strong>Linha inferior:</strong> Uma foto centralizada</li>
                <li>Cada foto é acompanhada de sua legenda logo abaixo</li>
                <li>Se houver menos de 3 fotos, o layout se ajusta automaticamente</li>
            </ul>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> Utilize o <strong>Banco de Imagens</strong> da plataforma para organizar as fotos do semestre antes de adicioná-las ao parecer. Assim, todas as fotos ficam centralizadas e fáceis de localizar.
</div>

