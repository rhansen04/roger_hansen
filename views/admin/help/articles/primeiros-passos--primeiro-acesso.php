<p class="lead">Acabou de receber seu acesso à plataforma? Siga este checklist de 6 etapas para configurar tudo corretamente e começar a usar os recursos pedagógicos e de gestão.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> Complete estas etapas na ordem sugerida. Cada passo prepara o terreno para o próximo. Todo o processo leva cerca de 15 minutos.
</div>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Entenda seu Papel na Plataforma</h6>
            <p>O sistema possui três papéis principais com permissões distintas. Seu papel define o que você vê e pode fazer:</p>
            <ul>
                <li><strong><span class="badge bg-success">Admin</span></strong> — Acesso total a todos os módulos. Gerencia escolas, usuários, cursos, matrículas, relatórios e configurações. Pode criar, editar e excluir qualquer registro.</li>
                <li><strong><span class="badge bg-primary">Professor</span></strong> — Trabalha com suas próprias turmas. Cria observações pedagógicas, pareceres descritivos, portfólios, planejamentos e gerencia o banco de imagens. Não pode aprovar documentos.</li>
                <li><strong><span class="badge bg-warning text-dark">Coordenador</span></strong> — Supervisiona o trabalho pedagógico. Visualiza todas as observações, pareceres e portfólios. Pode reabrir documentos finalizados e solicitar revisões com anotações. Não cria documentos pedagógicos diretamente.</li>
            </ul>
            <p>Além destes, existem os papéis <span class="badge bg-secondary">Student</span> (aluno — acessa cursos online, progresso e certificados) e <span class="badge bg-secondary">Parent</span> (responsável — acompanha o desempenho do filho).</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Conheça seu Dashboard Personalizado</h6>
            <p>Ao fazer login, você chega ao Dashboard. O conteúdo muda conforme seu papel:</p>
            <ul>
                <li><strong>Professor:</strong> cards de turmas ativas, alunos, cursos e pareceres pendentes. Tabela de cursos com progresso e observações recentes. Lista de turmas na barra lateral.</li>
                <li><strong>Coordenador:</strong> 8 cards com métricas gerais (turmas, crianças, professores, observações, escolas, cursos, matrículas, progresso). Relatório de cursos e observações de todos os professores.</li>
                <li><strong>Admin:</strong> visão completa com métricas de toda a plataforma, matrículas recentes, cursos populares, alunos com baixo progresso, contatos e observações.</li>
            </ul>
            <p>Clique no botão <strong>? Ajuda</strong> na barra superior para fazer o tour guiado da sua dashboard.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Cadastre a Escola</h6>
            <p>Vá em <strong>Cadastros &rarr; Escolas</strong> e clique em "Nova Escola". Preencha os dados:</p>
            <ul>
                <li><strong>Dados básicos:</strong> nome da escola, cidade, estado (lista de UFs), endereço completo</li>
                <li><strong>Contato:</strong> nome do responsável, telefone e e-mail da escola</li>
                <li><strong>Contrato:</strong> datas de início e fim do contrato, status (ativa ou inativa)</li>
                <li><strong>Logo:</strong> upload opcional da logo da escola (JPG/PNG/GIF, máximo 2MB)</li>
            </ul>
            <p>A escola é a base para vincular turmas, alunos e professores. Cadastre pelo menos uma antes de continuar.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Crie Turmas e Vincule Alunos</h6>
            <p>Acesse <strong>Ensino &rarr; Turmas</strong> para criar suas salas:</p>
            <ul>
                <li><strong>Criar turma:</strong> clique em "Nova Turma" e preencha nome, faixa etária, escola, período (manhã/tarde/integral) e ano letivo</li>
                <li><strong>Cadastrar alunos:</strong> em <strong>Cadastros &rarr; Alunos</strong>, adicione as crianças com nome, data de nascimento, escola e foto opcional</li>
                <li><strong>Vincular à turma:</strong> na tela da turma, clique em "Adicionar Alunos" e selecione as crianças cadastradas</li>
            </ul>
            <p>Com alunos vinculados à turma, você poderá criar observações, pareceres e portfólios.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Configure o Planejamento Pedagógico</h6>
            <p>Acesse <strong>Ensino &rarr; Planejamento</strong> para organizar suas aulas:</p>
            <ul>
                <li><strong>Escolha um template:</strong> selecione um modelo pré-configurado com seções e campos adequados à sua realidade</li>
                <li><strong>Crie uma submission:</strong> preencha os campos do template para a turma e período desejados</li>
                <li><strong>Calendário:</strong> visualize seus planejamentos no formato de calendário para ter uma visão temporal</li>
                <li><strong>Rotina diária:</strong> registre a sequência de atividades planejadas para cada dia</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">6</div>
        <div class="step-content">
            <h6>Explore Observações e Pareceres</h6>
            <p>Estes são os módulos centrais do trabalho pedagógico:</p>
            <ul>
                <li><strong>Professor cria:</strong> registre observações diárias sobre cada criança nos 6 eixos pedagógicos. O auto-save protege seu trabalho. Ao concluir, clique em "Finalizar".</li>
                <li><strong>Coordenador revisa:</strong> observações finalizadas aparecem para o coordenador, que pode aprovar ou solicitar revisão com anotações específicas.</li>
                <li><strong>Parecer descritivo:</strong> compile as observações em um parecer formal. Use a correção por IA para aprimorar o texto e exporte em PDF.</li>
                <li><strong>Portfólio:</strong> crie o registro coletivo da turma com fotos e textos organizados por eixo pedagógico.</li>
            </ul>
        </div>
    </div>
</div>

<div class="help-tip help-tip-success">
    <i class="fas fa-check-circle me-2"></i>
    <strong>Tudo pronto!</strong> Com esses 6 passos concluídos, sua plataforma está configurada. Explore os módulos de <a href="/admin/help/pedagogico/observacoes">Observações</a> e <a href="/admin/help/pedagogico/pareceres">Pareceres</a> para começar o trabalho pedagógico.
</div>
