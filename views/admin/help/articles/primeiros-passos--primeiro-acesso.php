<p class="lead">Acabou de receber seu acesso? Veja o checklist de configurações iniciais para deixar tudo pronto.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> Complete estas etapas na ordem sugerida para uma experiência mais fluida.
</div>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Faça login com suas credenciais</h6>
            <p>Acesse <code>/login</code> com o e-mail e senha fornecidos pelo administrador. Se esqueceu a senha, use a opção "Esqueci minha senha".</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Conheça o Dashboard</h6>
            <p>Explore os cards e tabelas do Dashboard. Clique no botão <strong>?</strong> na barra superior para fazer o tour guiado.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Cadastre sua primeira escola</h6>
            <p>Vá em <strong>Cadastros → Escolas</strong> e registre a escola onde atua. Isso será útil para vincular alunos e turmas.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Cadastre alunos</h6>
            <p>Em <strong>Cadastros → Alunos</strong>, adicione seus alunos com nome, e-mail e escola. Eles receberão acesso ao painel do aluno.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Explore um curso existente</h6>
            <p>Acesse <strong>Ensino → Cursos</strong> para ver os cursos disponíveis. Clique em um para ver seções, módulos e lições.</p>
        </div>
    </div>
</div>

<div class="help-tip help-tip-success">
    <i class="fas fa-check-circle me-2"></i>
    <strong>Tudo pronto!</strong> Agora você pode começar a matricular alunos nos cursos e acompanhar o progresso.
</div>
