<p class="lead">O relatório geral oferece uma visão consolidada de toda a plataforma: matrículas, progresso, desempenho em quizzes e tendências recentes.</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Acesse os Relatórios</h6>
            <p>No menu lateral, clique em <strong>Análises → Relatórios</strong>. A página é dividida em seções de dados.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Cards de Resumo</h6>
            <p>No topo, 6 cards com as métricas principais:</p>
            <ul>
                <li><strong>Total de Alunos</strong> — quantidade cadastrada</li>
                <li><strong>Cursos Ativos</strong> — cursos com status ativo</li>
                <li><strong>Total de Matrículas</strong> — todas as matrículas no sistema</li>
                <li><strong>Matrículas Ativas</strong> — atualmente ativas</li>
                <li><strong>Cursos Concluídos</strong> — matrículas com 100%</li>
                <li><strong>Progresso Médio</strong> — média geral de todas as matrículas ativas</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Performance por Curso</h6>
            <p>Tabela comparativa mostrando para cada curso:</p>
            <ul>
                <li><strong>Matrículas:</strong> Total de inscritos</li>
                <li><strong>Ativas:</strong> Badge verde com matrículas em andamento</li>
                <li><strong>Concluídas:</strong> Badge azul com conclusões</li>
                <li><strong>Progresso Médio:</strong> Barra visual com percentual</li>
                <li><strong>Taxa de Conclusão:</strong> Percentual calculado (concluídas/total). Cores: verde (≥50%), amarelo (≥25%), cinza (&lt;25%)</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Performance de Quizzes</h6>
            <p>Tabela com estatísticas de cada quiz:</p>
            <ul>
                <li><strong>Quiz e Curso:</strong> Identificação</li>
                <li><strong>Tentativas:</strong> Quantidade total realizadas</li>
                <li><strong>Nota Média:</strong> Verde (≥70%) ou amarelo (&lt;70%)</li>
                <li><strong>Aprovados:</strong> Quantidade que atingiu a nota mínima</li>
                <li><strong>Taxa de Aprovação:</strong> Verde (≥70%), amarelo (≥40%), vermelho (&lt;40%)</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Matrículas Recentes (30 dias)</h6>
            <p>Tabela diária mostrando o volume de novas matrículas com barra visual proporcional. Útil para identificar tendências e picos de inscrição.</p>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> Use o relatório para identificar cursos com baixa taxa de conclusão — pode indicar conteúdo muito longo, difícil ou desengajante. Quizzes com baixa aprovação podem precisar de ajustes na dificuldade ou nota mínima.
</div>
