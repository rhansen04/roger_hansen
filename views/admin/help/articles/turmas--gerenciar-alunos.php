<p class="lead">Gerencie a composição das turmas: veja quais alunos pertencem a cada grupo e mantenha as informações atualizadas.</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Visualizar alunos de uma turma</h6>
            <p>Na lista de turmas (<strong>Ensino → Turmas</strong>), cada turma mostra o resumo dos alunos vinculados. A quantidade de alunos aparece como badge na tabela.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Editar informações da turma</h6>
            <p>Clique no botão <strong>✏️ Editar</strong> para alterar:</p>
            <ul>
                <li>Nome, escola e professor responsável</li>
                <li>Faixa etária e período</li>
                <li>Ano letivo e status (ativa/inativa)</li>
            </ul>
            <p>Turmas inativas podem ser mantidas para fins de histórico e referência em planejamentos passados.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Relação com outros módulos</h6>
            <p>As turmas conectam diferentes partes do sistema:</p>
            <ul>
                <li><strong>Planejamentos:</strong> Cada planejamento é vinculado a uma turma</li>
                <li><strong>Escolas:</strong> A turma pertence a uma escola específica</li>
                <li><strong>Professores:</strong> Cada turma tem um professor responsável</li>
                <li><strong>Filtros:</strong> Use turmas para filtrar planejamentos e relatórios</li>
            </ul>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Organização:</strong> Use nomes descritivos que incluam nível, período e ano. Ex: "Maternal II - Tarde - 2026". Isso facilita a seleção em outros formulários.
</div>
