<p class="lead">Conheça a experiência do aluno (professor em formação) ao acessar os cursos na plataforma: desde o painel inicial até a conclusão com geração de certificado.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Contexto:</strong> Na Hansen Educacional, os "alunos" dos cursos são os <strong>professores em formação continuada</strong>. Eles acessam os cursos pela área <strong>Minha Conta</strong> (/minha-conta), separada da área administrativa.
</div>

<h5 class="mt-4 mb-3"><i class="fas fa-th-large me-2 text-primary"></i>Painel do aluno (Dashboard)</h5>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Acesse o painel "Minha Conta"</h6>
            <p>O aluno acessa a plataforma e é direcionado ao seu painel pessoal em <strong>/minha-conta</strong>. Nesta página, são exibidos cards com todos os cursos em que o aluno está matriculado.</p>
            <ul>
                <li>Cada card mostra o <strong>nome do curso</strong> em destaque</li>
                <li>Uma <strong>barra de progresso</strong> visual indica a porcentagem de conclusão do curso (0% a 100%)</li>
                <li>Botão de ação contextual:
                    <ul>
                        <li><strong>"Iniciar curso"</strong> — quando o aluno ainda não começou (0% de progresso)</li>
                        <li><strong>"Continuar curso"</strong> — quando o aluno já iniciou mas não concluiu</li>
                    </ul>
                </li>
                <li>Cursos concluídos (100%) exibem um badge de conclusão e opção de gerar certificado</li>
            </ul>
        </div>
    </div>
</div>

<h5 class="mt-4 mb-3"><i class="fas fa-book-open me-2 text-primary"></i>Página de detalhes do curso</h5>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Visualize os módulos do curso</h6>
            <p>Ao clicar em um curso, o aluno acessa a página de detalhes que exibe os módulos do curso em formato de cards organizados. Cada card de módulo mostra:</p>
            <ul>
                <li><strong>Nome do módulo:</strong> título descritivo do conteúdo</li>
                <li><strong>Descrição:</strong> resumo do que será abordado no módulo</li>
                <li><strong>Contagem de seções/aulas:</strong> quantidade de lições dentro do módulo</li>
                <li><strong>Barra de progresso:</strong> barra visual mostrando o avanço do aluno dentro daquele módulo específico</li>
                <li><strong>Badge de status:</strong> indicador colorido do estado atual do módulo para o aluno</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Entenda os status dos módulos</h6>
            <p>Cada módulo possui um dos seguintes status para o aluno:</p>
            <ul>
                <li><span class="badge bg-secondary">Não iniciado</span> — o aluno ainda não acessou nenhuma aula do módulo</li>
                <li><span class="badge bg-primary">Em andamento</span> — o aluno já completou pelo menos uma aula, mas ainda há aulas pendentes</li>
                <li><span class="badge bg-success">Concluído</span> — todas as aulas do módulo foram completadas pelo aluno</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Indicadores visuais das aulas (lições)</h6>
            <p>Dentro de cada módulo, as aulas individuais possuem indicadores visuais para mostrar o progresso:</p>
            <ul>
                <li><strong><span style="color: #28a745;"><i class="fas fa-check-circle"></i></span> Círculo verde com check:</strong> aula concluída — o aluno já assistiu/completou todo o conteúdo</li>
                <li><strong><span style="color: #6c757d;"><i class="fas fa-play-circle"></i></span> Círculo cinza com play:</strong> aula não iniciada — disponível para o aluno acessar</li>
                <li><strong><span style="color: #6c757d;"><i class="fas fa-lock"></i></span> Ícone de cadeado:</strong> aula bloqueada — o aluno não está matriculado neste curso ou não atende aos pré-requisitos</li>
            </ul>
        </div>
    </div>
</div>

<h5 class="mt-4 mb-3"><i class="fas fa-th-list me-2 text-primary"></i>Categorias de conteúdo</h5>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Seção de categorias de conteúdo</h6>
            <p>Na página do curso, uma seção especial apresenta as categorias de conteúdo disponíveis com contadores e links diretos:</p>
            <ul>
                <li><strong><i class="fas fa-cubes me-1"></i> Módulos:</strong> quantidade total de módulos do curso, com link para a lista de módulos</li>
                <li><strong><i class="fas fa-play-circle me-1"></i> Aulas:</strong> quantidade total de aulas (lições) em todos os módulos, com acesso direto</li>
                <li><strong><i class="fas fa-clipboard-check me-1"></i> Testes Avaliativos:</strong> quantidade de testes/avaliações disponíveis no curso, com link para acessá-los</li>
                <li><strong><i class="fas fa-paperclip me-1"></i> Material Complementar:</strong> quantidade de materiais extras (PDFs, documentos de apoio) disponíveis para download</li>
            </ul>
            <p>Cada categoria exibe o número total de itens e permite navegação direta ao conteúdo correspondente.</p>
        </div>
    </div>
</div>

<h5 class="mt-4 mb-3"><i class="fas fa-certificate me-2 text-primary"></i>Certificado de conclusão</h5>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">6</div>
        <div class="step-content">
            <h6>Geração do certificado</h6>
            <p>Quando o aluno atinge <strong>100% de progresso</strong> no curso (todas as aulas e módulos concluídos), a opção de gerar certificado é habilitada automaticamente.</p>
            <ul>
                <li>O botão de certificado aparece tanto no card do curso no dashboard quanto na página de detalhes</li>
                <li>O certificado é gerado em formato PDF com os dados do aluno, nome do curso e data de conclusão</li>
                <li>O certificado pode ser baixado e impresso pelo aluno</li>
                <li>Apenas cursos com 100% de conclusão habilitam a geração — não há certificado parcial</li>
            </ul>
        </div>
    </div>
</div>

