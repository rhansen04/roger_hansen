<p class="lead">Após criar o parecer descritivo, você pode editar livremente o texto da criança e utilizar a correção automática por Inteligência Artificial para aprimorar a escrita sem alterar o conteúdo pedagógico.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Você sabia?</strong> A edição do texto no parecer <strong>nunca altera</strong> a observação original vinculada. Você pode modificar o texto à vontade com total segurança.
</div>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Abra o parecer em modo de edição</h6>
            <p>Na listagem de pareceres, clique no parecer desejado e depois no botão <strong>"Editar"</strong>. A página de edição utiliza uma interface de abas (tabs) para organizar as diferentes seções do documento.</p>
            <ul>
                <li>As abas disponíveis são: <strong>Capa</strong>, <strong>Texto da Criança</strong> e <strong>Fotos dos Eixos</strong></li>
                <li>Você pode navegar entre as abas livremente, salvando as alterações em cada uma</li>
                <li>Pareceres com status "finalizado" não podem ser editados — é necessário solicitar revisão primeiro</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Aba "Capa" — Foto do aluno</h6>
            <p>Na primeira aba, você gerencia a foto de capa do parecer descritivo:</p>
            <ul>
                <li><strong>Upload de foto:</strong> Envie uma foto do aluno que será exibida na capa do documento PDF</li>
                <li><strong>Trocar foto:</strong> Caso já exista uma foto, você pode substituí-la enviando uma nova imagem</li>
                <li>A foto aparece centralizada na capa com fundo verde institucional, junto com o nome do aluno, turma e semestre</li>
                <li>Formatos aceitos: JPG, PNG. Recomenda-se fotos em orientação retrato (vertical) para melhor enquadramento</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Aba "Texto da Criança" — Edição livre</h6>
            <p>Esta é a aba principal de edição do conteúdo narrativo do parecer:</p>
            <ul>
                <li>O campo de texto exibe a narrativa compilada automaticamente a partir da observação vinculada</li>
                <li>Você pode <strong>editar livremente</strong> o texto: reescrever trechos, adicionar informações, reorganizar parágrafos</li>
                <li>A observação original permanece <strong>intacta</strong> — as alterações são salvas apenas no campo do parecer</li>
                <li>Use o editor para refinar a linguagem, personalizar a narrativa e garantir que o texto represente fielmente o desenvolvimento da criança</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Correção Automática por IA (Gemini)</h6>
            <p>O recurso de correção automática utiliza a inteligência artificial do Google Gemini para aprimorar o texto sem alterar seu conteúdo pedagógico.</p>
            <ul>
                <li><strong>Como usar:</strong> Clique no botão <strong>"Correção Automática IA"</strong> localizado abaixo do campo de texto</li>
                <li><strong>O que a IA corrige:</strong>
                    <ul>
                        <li>Erros de ortografia e gramática</li>
                        <li>Concordância verbal e nominal</li>
                        <li>Fluidez e coesão textual</li>
                        <li>Pontuação e acentuação</li>
                    </ul>
                </li>
                <li><strong>O que a IA preserva:</strong>
                    <ul>
                        <li>Todo o conteúdo pedagógico original</li>
                        <li>As observações sobre o desenvolvimento da criança</li>
                        <li>O tom afetuoso e profissional do relato</li>
                        <li>Nomes próprios e referências específicas</li>
                    </ul>
                </li>
                <li><strong>Resultado:</strong> O texto corrigido é salvo no campo <strong>student_text_edited</strong>, mantendo o texto original como referência</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Revisão e ajustes finais</h6>
            <p>Após a correção da IA, revise o resultado e faça ajustes manuais se necessário:</p>
            <ul>
                <li>O texto corrigido pela IA pode ser <strong>editado manualmente</strong> para ajustes finos</li>
                <li>Compare o texto original com o corrigido para verificar se nenhuma informação foi perdida</li>
                <li>Você pode executar a correção por IA quantas vezes precisar</li>
                <li>Salve as alterações clicando no botão <strong>"Salvar"</strong> ao final da página</li>
            </ul>
        </div>
    </div>
</div>

