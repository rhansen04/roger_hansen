<p class="lead">O painel administrativo da Hansen Educacional é seu centro de controle para gerenciar alunos, cursos, matrículas e muito mais. Vamos conhecer cada área.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Você sabia?</strong> Você pode iniciar um tour interativo em qualquer página clicando no botão <strong>?</strong> na barra superior. O tour destaca cada elemento e explica sua função.
</div>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Dashboard — Sua Página Inicial</h6>
            <p>Ao fazer login, você chega ao <strong>Dashboard</strong>. Aqui você encontra:</p>
            <ul>
                <li><strong>Cards de resumo:</strong> Total de alunos, matrículas ativas, cursos e contatos</li>
                <li><strong>Matrículas recentes:</strong> Últimas matrículas realizadas com status e progresso</li>
                <li><strong>Cursos populares:</strong> Ranking dos cursos com mais matrículas</li>
                <li><strong>Ações rápidas:</strong> Atalhos para as tarefas mais comuns</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Menu Lateral — Navegação Principal</h6>
            <p>O menu lateral organiza todas as funcionalidades em seções:</p>
            <ul>
                <li><strong>Cadastros:</strong> Alunos, Escolas, Usuários e Responsáveis</li>
                <li><strong>Ensino:</strong> Cursos, Matrículas, Turmas, Planejamentos e Templates</li>
                <li><strong>Comunicação:</strong> Contatos recebidos e Perguntas dos alunos</li>
                <li><strong>Análises:</strong> Dashboard de Vídeos e Relatórios de desempenho</li>
                <li><strong>Ajuda:</strong> Tours interativos e esta Central de Ajuda</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Barra Superior — Ferramentas Rápidas</h6>
            <p>Na barra superior você encontra:</p>
            <ul>
                <li><strong>Usuário logado:</strong> Seu nome e papel (admin/instructor)</li>
                <li><strong>Botão ?:</strong> Inicia o tour interativo da página atual</li>
                <li><strong>Modo escuro:</strong> Alterna entre tema claro e escuro</li>
                <li><strong>Ver Site Público:</strong> Abre o site em outra aba</li>
            </ul>
        </div>
    </div>
</div>

