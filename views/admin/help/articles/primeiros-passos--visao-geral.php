<p class="lead">A plataforma Hansen Educacional é um sistema completo de gestão pedagógica e educacional. Conheça os 12 módulos que compõem o painel e como eles se conectam para facilitar o dia a dia de professores, coordenadores e administradores.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Você sabia?</strong> Você pode iniciar um tour interativo em qualquer página clicando no botão <strong>?</strong> na barra superior. O tour destaca cada elemento e explica sua função passo a passo.
</div>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Dashboard — Página Inicial Personalizada</h6>
            <p>O Dashboard é a primeira tela após o login e se adapta ao papel do usuário:</p>
            <ul>
                <li><strong>Professor:</strong> vê suas turmas ativas, alunos vinculados, cursos em andamento e pareceres pendentes</li>
                <li><strong>Coordenador:</strong> visão geral de todas as turmas, professores, observações recentes e progresso dos cursos</li>
                <li><strong>Admin:</strong> painel completo com métricas de alunos, matrículas, cursos, contatos, escolas, horas assistidas, quizzes e observações</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Turmas — Organização por Sala</h6>
            <p>Gerencie turmas de alunos com controle completo:</p>
            <ul>
                <li><strong>Criar turma:</strong> defina nome, faixa etária, escola, período (manhã/tarde/integral) e ano letivo</li>
                <li><strong>Ativar/desativar:</strong> turmas inativas ficam ocultas nas listagens, mas os dados são preservados</li>
                <li><strong>Vincular alunos:</strong> adicione crianças à turma para que apareçam nas observações, pareceres e portfólios</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Alunos — Cadastro Completo</h6>
            <p>Cada criança possui um perfil detalhado:</p>
            <ul>
                <li><strong>Dados pessoais:</strong> nome, data de nascimento (idade calculada automaticamente), escola</li>
                <li><strong>Foto:</strong> upload com preview em tempo real, exibida em observações e pareceres</li>
                <li><strong>Resumo por IA:</strong> geração automática de um resumo pedagógico com base nas observações registradas</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Observações Pedagógicas — Registro Diário</h6>
            <p>O professor registra observações sobre cada criança, organizadas em 6 eixos pedagógicos:</p>
            <ul>
                <li><strong>Auto-save:</strong> o texto é salvo automaticamente a cada pausa na digitação, sem risco de perder conteúdo</li>
                <li><strong>Perguntas numeradas:</strong> cada eixo exibe suas perguntas orientadoras numeradas, cada uma com seu próprio campo de resposta obrigatório</li>
                <li><strong>Finalização:</strong> ao finalizar, a observação fica protegida contra edição e disponível para o coordenador</li>
                <li><strong>Feedbacks da Coordenação:</strong> coordenadores podem registrar comentários diretamente na observação, com notificação ao professor</li>
                <li><strong>Semestral:</strong> as observações são separadas por semestre (1º ou 2º), facilitando o acompanhamento longitudinal</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Parecer Descritivo — Síntese por Criança</h6>
            <p>O parecer compila as observações em um documento formal:</p>
            <ul>
                <li><strong>Compilar:</strong> puxa automaticamente os textos das observações para montar o parecer</li>
                <li><strong>Correção por IA:</strong> sugestões de melhoria no texto com um clique</li>
                <li><strong>Fotos:</strong> adicione fotos da criança em atividades para ilustrar o parecer</li>
                <li><strong>Exportar PDF:</strong> gere um PDF formatado pronto para impressão ou envio aos responsáveis</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">6</div>
        <div class="step-content">
            <h6>Portfólio da Turma — Registro Coletivo</h6>
            <p>Documento coletivo por turma com 5 eixos pedagógicos:</p>
            <ul>
                <li><strong>Fotos por eixo:</strong> selecione fotos do Banco de Imagens interno da turma para cada eixo, com legendas descritivas</li>
                <li><strong>Texto coletivo:</strong> descreva as vivências e aprendizagens do grupo</li>
                <li><strong>Feedbacks da Coordenação:</strong> coordenadores podem registrar comentários diretamente no portfólio, com notificação automática ao professor</li>
                <li><strong>Exportar PDF:</strong> gere um portfólio visual completo da turma</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">7</div>
        <div class="step-content">
            <h6>Banco de Imagens — Acervo Organizado</h6>
            <p>Repositório de fotos organizado por turma:</p>
            <ul>
                <li><strong>Upload:</strong> envie múltiplas fotos de uma vez com arrastar e soltar</li>
                <li><strong>Legendas:</strong> adicione descrições para facilitar a busca e o uso em pareceres e portfólios</li>
                <li><strong>Por turma:</strong> cada turma possui seu próprio acervo, mantendo tudo organizado</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">8</div>
        <div class="step-content">
            <h6>Planejamento Pedagógico — Estrutura Curricular</h6>
            <p>Organize o planejamento das turmas com flexibilidade:</p>
            <ul>
                <li><strong>Templates:</strong> modelos pré-configurados com seções e campos prontos para preenchimento</li>
                <li><strong>Calendário:</strong> visualize os planejamentos no formato de calendário</li>
                <li><strong>Rotina diária:</strong> registre a sequência de atividades do dia</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">9</div>
        <div class="step-content">
            <h6>Material de Apoio — Biblioteca Digital</h6>
            <p>Compartilhe materiais com a equipe pedagógica:</p>
            <ul>
                <li><strong>Pastas hierárquicas:</strong> organize materiais em pastas e subpastas por tema ou área</li>
                <li><strong>Upload/download:</strong> envie PDFs, documentos, imagens e outros arquivos</li>
                <li><strong>Acesso por papel:</strong> controle quem pode ver e baixar cada material</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">10</div>
        <div class="step-content">
            <h6>Notificações — Comunicação Interna</h6>
            <p>Sistema de alertas e comunicação entre usuários:</p>
            <ul>
                <li><strong>Ícone de sino:</strong> na barra superior, com badge indicando notificações não lidas</li>
                <li><strong>Lida/não lida:</strong> marque notificações como lidas individualmente ou em lote</li>
                <li><strong>Workflow de aprovação:</strong> coordenadores recebem notificações quando observações/pareceres são finalizados</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">11</div>
        <div class="step-content">
            <h6>Cursos — Ensino a Distância</h6>
            <p>Plataforma completa de cursos online:</p>
            <ul>
                <li><strong>Módulos e lições:</strong> estruture o conteúdo em módulos com lições sequenciais</li>
                <li><strong>Quizzes:</strong> avaliações com correção automática ao final de cada módulo</li>
                <li><strong>Acompanhamento:</strong> progresso individual de cada aluno com percentual de conclusão</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">12</div>
        <div class="step-content">
            <h6>Relatórios — Análise de Dados</h6>
            <p>Painéis analíticos para tomada de decisão:</p>
            <ul>
                <li><strong>Relatório Geral:</strong> visão consolidada de todas as métricas da plataforma</li>
                <li><strong>Baixo Desempenho:</strong> alunos com menos de 20% de progresso após 7+ dias matriculados</li>
                <li><strong>Tracking de Vídeos:</strong> horas assistidas, vídeos concluídos e engajamento</li>
                <li><strong>Relatório de Cursos:</strong> matrículas, progresso médio e taxa de conclusão por curso</li>
            </ul>
        </div>
    </div>
</div>

<h6 class="mt-4 mb-3">Organização do Menu Lateral</h6>
<p>O menu lateral se adapta ao perfil do usuário logado:</p>

<p><strong>Professor</strong> — menu simplificado em ordem pedagógica:</p>
<ul>
    <li>Turmas → Observações → Planejamentos (com sub-item <strong>Rotina Semanal</strong>) → Material de Apoio → Pareceres → Portfólios → Banco de Imagens</li>
</ul>

<p><strong>Coordenador</strong> — menu dedicado de supervisão:</p>
<ul>
    <li>Turmas → Observações → Pareceres → Portfólios → Gestão de Professores → Acompanhamento de Cursos</li>
</ul>

<p><strong>Administrador</strong> — 3 módulos globais fixos + navegação contextual por escola:</p>
<ul>
    <li><strong>Módulos globais:</strong> Escolas, Usuários, Cursos + Material de Apoio</li>
    <li><strong>Contexto de escola:</strong> ao clicar em <strong>"Entrar no Ambiente"</strong> de uma escola, um submenu filtrado é ativado: Turmas, Alunos, Observações, Pareceres, Planejamentos, Portfólios, Banco de Imagens — todos filtrados pela escola selecionada</li>
</ul>

