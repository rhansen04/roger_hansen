<h5 class="mt-4 mb-3"><i class="fas fa-chart-line me-2 text-primary"></i>Como usar o relatório</h5>

<p>O relatório de cursos é uma ferramenta de gestão para acompanhar o programa de formação continuada. Algumas formas de utilização:</p>

<ul>
    <li><strong>Identificar cursos com baixo engajamento:</strong> cursos com muitas matrículas mas progresso médio baixo podem indicar conteúdo desinteressante ou dificuldade de acesso</li>
    <li><strong>Acompanhar metas de conclusão:</strong> compare o número de concluídos com o total de matriculados para verificar se as metas de formação estão sendo atingidas</li>
    <li><strong>Planejar novas formações:</strong> cursos com alto engajamento e conclusão indicam temas de interesse dos professores, orientando a criação de novos conteúdos</li>
    <li><strong>Relatórios para a direção:</strong> exporte o CSV e utilize em apresentações de resultados do programa de formação</li>
</ul>

<div class="help-tip help-tip-success">
    <i class="fas fa-check-circle me-2"></i>
    <strong>Dica:</strong> Consulte este relatório periodicamente (semanal ou quinzenalmente) para acompanhar a evolução dos professores nos cursos. Se identificar um professor com progresso estagnado, converse diretamente para entender se há alguma dificuldade ou necessidade de suporte.
</div>
