<p class="lead">O Relatório de Cursos oferece uma visão consolidada do engajamento dos professores com os cursos de formação continuada, permitindo acompanhar matrículas, progresso e conclusões em um único painel.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Acesso:</strong> Este relatório está disponível para <strong>Administradores</strong> e <strong>Coordenadores</strong>. Professores não têm acesso a esta seção.
</div>

<h5 class="mt-4 mb-3"><i class="fas fa-chart-bar me-2 text-primary"></i>Acessando o relatório</h5>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Navegue até o relatório</h6>
            <p>No menu lateral, acesse <strong>Análises → Relatório de Cursos</strong>. A página do relatório será carregada com dados atualizados em tempo real.</p>
            <ul>
                <li>Os dados são calculados no momento do acesso, refletindo o estado atual de todos os cursos e matrículas</li>
                <li>Não é necessário selecionar filtros — o relatório apresenta automaticamente todos os cursos cadastrados</li>
            </ul>
        </div>
    </div>
</div>

<h5 class="mt-4 mb-3"><i class="fas fa-tachometer-alt me-2 text-primary"></i>Cards de resumo</h5>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Visualize os indicadores gerais</h6>
            <p>No topo da página, três cards de resumo apresentam os números-chave da plataforma de cursos:</p>
            <ul>
                <li><strong><i class="fas fa-graduation-cap me-1 text-primary"></i> Total de Cursos:</strong> quantidade total de cursos cadastrados na plataforma (ativos e inativos)</li>
                <li><strong><i class="fas fa-user-graduate me-1 text-success"></i> Total de Matrículas:</strong> soma de todas as matrículas ativas em todos os cursos — se um professor está matriculado em 3 cursos, conta como 3 matrículas</li>
                <li><strong><i class="fas fa-check-double me-1 text-warning"></i> Total de Conclusões:</strong> quantidade de matrículas com 100% de progresso em todos os cursos</li>
            </ul>
            <p>Esses cards fornecem uma visão instantânea da saúde geral do programa de formação continuada.</p>
        </div>
    </div>
</div>

<h5 class="mt-4 mb-3"><i class="fas fa-table me-2 text-primary"></i>Tabela detalhada por curso</h5>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Analise os dados por curso</h6>
            <p>Abaixo dos cards de resumo, uma tabela detalhada lista todos os cursos com as seguintes colunas:</p>
            <ul>
                <li><strong>Curso:</strong> nome do curso conforme cadastrado no sistema</li>
                <li><strong>Total de Matriculados:</strong> quantidade total de alunos (professores) matriculados naquele curso</li>
                <li><strong>Professores Matriculados:</strong> detalhamento dos matriculados que possuem perfil de professor na plataforma</li>
                <li><strong>Ativos:</strong> quantidade de alunos que iniciaram o curso (progresso maior que 0%) mas ainda não concluíram</li>
                <li><strong>Concluídos:</strong> quantidade de alunos que atingiram 100% de progresso no curso</li>
                <li><strong>Progresso Médio (%):</strong> média aritmética do progresso de todos os alunos matriculados</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Barra visual de progresso</h6>
            <p>A coluna de <strong>Progresso Médio</strong> exibe, além do valor percentual numérico, uma <strong>barra visual colorida</strong> que facilita a comparação rápida entre cursos:</p>
            <ul>
                <li>A largura da barra é proporcional ao percentual (0% a 100%)</li>
                <li>A cor da barra varia conforme o progresso:
                    <ul>
                        <li><span class="badge bg-danger">Vermelho</span> — progresso abaixo de 25%</li>
                        <li><span class="badge bg-warning text-dark">Amarelo</span> — progresso entre 25% e 50%</li>
                        <li><span class="badge bg-info">Azul</span> — progresso entre 50% e 75%</li>
                        <li><span class="badge bg-success">Verde</span> — progresso acima de 75%</li>
                    </ul>
                </li>
                <li>Isso permite identificar visualmente quais cursos têm maior engajamento dos professores</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Ordenação da tabela</h6>
            <p>A tabela é ordenada por padrão pelo <strong>número de matriculados</strong> (do maior para o menor), facilitando a identificação dos cursos mais populares.</p>
            <ul>
                <li>Os cursos com mais matrículas aparecem no topo da lista</li>
                <li>Cursos sem nenhuma matrícula aparecem no final, com todas as colunas zeradas</li>
            </ul>
        </div>
    </div>
</div>

