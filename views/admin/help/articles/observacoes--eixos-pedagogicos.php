<h6 class="fw-bold mt-4 mb-3">6. PCA (Programa Comunicação Ativa)</h6>

<p>O <strong>PCA (Programa Comunicação Ativa)</strong> é dedicado à comunicação em todas as suas formas: verbal, não-verbal, expressiva e receptiva. Este eixo é fundamental para acompanhar o desenvolvimento linguístico e comunicacional da criança.</p>

<p><strong>O que observar:</strong></p>
<ul>
    <li>Expressão verbal: clareza na fala, construção de frases, uso de vocabulário adequado à faixa etária</li>
    <li>Desenvolvimento de vocabulário: incorporação de palavras novas, uso contextualizado</li>
    <li>Habilidades de conversa: capacidade de manter diálogos, respeitar turnos de fala, ouvir o outro</li>
    <li>Comunicação não-verbal: uso de gestos, expressões faciais, linguagem corporal para se comunicar</li>
    <li>Autonomia comunicativa: capacidade de expressar necessidades, desejos e sentimentos</li>
    <li>Interação social através da linguagem: como usa a comunicação para se relacionar com pares e adultos</li>
</ul>

<p><strong>Exemplos de frases:</strong></p>
<ul>
    <li><em>"Expressa suas necessidades e desejos de forma clara e articulada, utilizando frases completas e vocabulário diversificado."</em></li>
    <li><em>"Tem desenvolvido a habilidade de esperar sua vez de falar nas rodas de conversa, demonstrando respeito pela fala dos colegas."</em></li>
    <li><em>"Utiliza gestos e expressões faciais de forma complementar à fala, enriquecendo sua comunicação com interlocutores."</em></li>
</ul>

