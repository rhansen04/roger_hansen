<p class="lead">Cada observação pedagógica é composta por 6 eixos temáticos. Entender o que observar e registrar em cada eixo é fundamental para um acompanhamento completo do desenvolvimento da criança.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> Não se preocupe em escrever textos formais. Registre o que você observou de forma objetiva e descritiva. Esses registros serão a base para o parecer descritivo e para o Resumo IA — quanto mais detalhados, melhor será o resultado final.
</div>

<h6 class="fw-bold mt-4 mb-3">1. Observação Geral</h6>

<p>O eixo de <strong>Observação Geral</strong> é o espaço para registrar notas amplas sobre o desenvolvimento global da criança, englobando aspectos que não se encaixam nos eixos específicos.</p>

<p><strong>O que observar:</strong></p>
<ul>
    <li>Como a criança interage socialmente com colegas e adultos</li>
    <li>Estado emocional predominante: segurança, ansiedade, alegria, timidez</li>
    <li>Comportamento durante a rotina escolar: chegada, despedida, transições entre atividades</li>
    <li>Nível de autonomia nas tarefas do dia a dia (alimentação, higiene, organização)</li>
    <li>Capacidade de seguir combinados e regras do grupo</li>
    <li>Adaptação ao ambiente escolar e mudanças na rotina</li>
</ul>

<p><strong>Exemplos de frases:</strong></p>
<ul>
    <li><em>"Demonstra segurança ao se separar dos pais na chegada, cumprimentando os colegas espontaneamente."</em></li>
    <li><em>"Tem buscado maior autonomia nas atividades de higiene, lavando as mãos sem precisar de orientação."</em></li>
    <li><em>"Apresenta dificuldade em lidar com frustrações durante jogos coletivos, necessitando mediação do professor."</em></li>
</ul>

<hr class="my-4">

<h6 class="fw-bold mt-4 mb-3">2. Eixo de Movimento</h6>

<p>O <strong>Eixo de Movimento</strong> registra como a criança se relaciona com atividades que envolvem o corpo em movimento, desenvolvimento motor amplo e consciência espacial.</p>

<p><strong>O que observar:</strong></p>
<ul>
    <li>Habilidades motoras amplas: correr, pular, saltar, rolar, escalar</li>
    <li>Equilíbrio e coordenação corporal em diferentes superfícies e situações</li>
    <li>Consciência espacial: noção de distância, altura, profundidade</li>
    <li>Lateralidade: uso preferencial de uma das mãos, cruzamento de linha média</li>
    <li>Participação em jogos corporais e brincadeiras de movimento</li>
    <li>Confiança e disposição para enfrentar desafios motores</li>
</ul>

<p><strong>Exemplos de frases:</strong></p>
<ul>
    <li><em>"Consegue equilibrar-se na trave baixa com segurança, demonstrando evolução em relação ao início do semestre."</em></li>
    <li><em>"Participa ativamente das atividades de circuito motor, mas ainda apresenta cautela ao escalar estruturas mais altas."</em></li>
    <li><em>"Mostra boa consciência espacial ao desviar de obstáculos durante brincadeiras de corrida no pátio."</em></li>
</ul>

<hr class="my-4">

<h6 class="fw-bold mt-4 mb-3">3. Eixo Manual (Atividade Manual)</h6>

<p>O <strong>Eixo Manual</strong> abrange todas as atividades que envolvem habilidades motoras finas, trabalhos manuais e expressão artística através de materiais concretos.</p>

<p><strong>O que observar:</strong></p>
<ul>
    <li>Coordenação motora fina: preensão do lápis, uso de tesoura, manipulação de objetos pequenos</li>
    <li>Desenho e pintura: traço, uso de cores, representação figurativa, criatividade</li>
    <li>Recorte e colagem: precisão, controle da tesoura, composição</li>
    <li>Modelagem com massinha ou argila: criação de formas, detalhamento</li>
    <li>Atividades de construção: blocos, encaixes, montagens</li>
    <li>Interesse e persistência nas atividades manuais</li>
</ul>

<p><strong>Exemplos de frases:</strong></p>
<ul>
    <li><em>"Evoluiu significativamente no recorte com tesoura, conseguindo seguir linhas curvas com boa precisão."</em></li>
    <li><em>"Seus desenhos passaram a incluir mais detalhes: roupas nas figuras humanas, elementos do cenário e uso variado de cores."</em></li>
    <li><em>"Demonstra grande interesse por atividades de modelagem, criando figuras tridimensionais com riqueza de detalhes."</em></li>
</ul>

<hr class="my-4">

<h6 class="fw-bold mt-4 mb-3">4. Eixo Musical (Atividade Musical)</h6>

<p>O <strong>Eixo Musical</strong> registra a relação da criança com a música em suas diversas formas: escuta, produção sonora, ritmo e expressão corporal musicada.</p>

<p><strong>O que observar:</strong></p>
<ul>
    <li>Percepção rítmica: capacidade de acompanhar ritmos, bater palmas no tempo</li>
    <li>Expressão vocal: participação em cantigas, afinação, volume, entoação</li>
    <li>Exploração de instrumentos musicais: curiosidade, forma de manipulação, criação de sons</li>
    <li>Escuta ativa: atenção a sons do ambiente, diferenciação de timbres, apreciação musical</li>
    <li>Movimento com música: dança, expressão corporal em resposta a estímulos musicais</li>
    <li>Criação e improvisação musical: inventar melodias, ritmos ou letras</li>
</ul>

<p><strong>Exemplos de frases:</strong></p>
<ul>
    <li><em>"Acompanha ritmos simples com instrumentos de percussão, mantendo a pulsação de forma consistente."</em></li>
    <li><em>"Canta as cantigas do repertório da turma com entusiasmo, memorizando letras e melodias com facilidade."</em></li>
    <li><em>"Explora o xilofone de forma criativa, experimentando diferentes combinações de notas e criando pequenas melodias."</em></li>
</ul>

<hr class="my-4">

<h6 class="fw-bold mt-4 mb-3">5. Eixo de Contos (Atividade de Contos)</h6>

<p>O <strong>Eixo de Contos</strong> abrange o universo da literatura e da narrativa: como a criança se envolve com histórias, sua compreensão narrativa e capacidade de imaginar e recontar.</p>

<p><strong>O que observar:</strong></p>
<ul>
    <li>Engajamento com histórias: atenção durante a leitura, demonstração de interesse, pedido de repetição</li>
    <li>Compreensão narrativa: entendimento da sequência de eventos, identificação de personagens e conflitos</li>
    <li>Imaginação e fantasia: capacidade de criar cenários, atribuir falas a personagens, inventar finais alternativos</li>
    <li>Dramatização: participação em encenações de histórias, uso de expressões corporais e vocais</li>
    <li>Reconto: capacidade de recontar a história com suas próprias palavras, mantendo a sequência lógica</li>
    <li>Relação pessoal com as narrativas: conexões entre a história e suas experiências</li>
</ul>

<p><strong>Exemplos de frases:</strong></p>
<ul>
    <li><em>"Mantém atenção durante toda a contação de histórias, fazendo perguntas pertinentes sobre os personagens e o enredo."</em></li>
    <li><em>"Consegue recontar histórias curtas mantendo a sequência dos eventos principais, utilizando expressões como 'era uma vez' e 'e então'."</em></li>
    <li><em>"Participa com entusiasmo das dramatizações, assumindo papéis de personagens e improvisando falas coerentes."</em></li>
</ul>

