                <td>Obrigatório — deve corrigir e refinalizar</td>
            </tr>
            <tr>
                <td><strong>Impacto no status do documento</strong></td>
                <td>Nenhum — status permanece o mesmo</td>
                <td>Muda o status para "Revisão Solicitada"</td>
            </tr>
            <tr>
                <td><strong>Uso ideal</strong></td>
                <td>Orientações, sugestões, elogios, avisos</td>
                <td>Correções obrigatórias antes da aprovação</td>
            </tr>
        </tbody>
    </table>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Boas práticas para coordenadores:</strong> Use os feedbacks para criar uma cultura de feedback contínuo e positivo. Reserve a Solicitação de Revisão apenas para quando o documento precisar de correções antes de ser aprovado formalmente.
</div>

