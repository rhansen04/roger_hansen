<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Como registrar um feedback (visão do Coordenador)</h6>
            <p>Para registrar um feedback em um documento:</p>
            <ul>
                <li>Abra o documento desejado (observação, parecer ou portfólio)</li>
                <li>Role até a seção <strong>"Feedbacks da Coordenação"</strong> na parte inferior da página</li>
                <li>Digite o feedback no campo de texto disponível</li>
                <li>Clique em <strong>"Registrar Feedback"</strong> para salvar</li>
            </ul>
            <p>O feedback é salvo imediatamente e o professor recebe uma notificação interna.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Notificação automática ao professor</h6>
            <p>Ao registrar um feedback, o sistema envia automaticamente uma notificação para o professor autor do documento:</p>
            <ul>
                <li>A notificação aparece no <strong>sino de notificações</strong> (<i class="fas fa-bell"></i>) na barra superior</li>
                <li>A notificação inclui: nome do coordenador que registrou o feedback, nome do documento e link direto para visualizá-lo</li>
                <li>O professor pode clicar na notificação para acessar o documento e ler o feedback</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Como visualizar feedbacks recebidos (visão do Professor)</h6>
            <p>O professor visualiza os feedbacks diretamente no documento:</p>
            <ul>
                <li>Abra o documento correspondente (a notificação traz o link direto)</li>
                <li>Role até a seção <strong>"Feedbacks da Coordenação"</strong> — os feedbacks são exibidos em ordem cronológica</li>
                <li>Cada feedback mostra: <strong>nome do coordenador</strong>, <strong>data e hora</strong> do registro e o <strong>texto completo</strong></li>
            </ul>
            <p>Os feedbacks são apenas para leitura pelo professor — não é possível editar ou excluir feedbacks registrados pela coordenação.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Histórico de feedbacks</h6>
            <p>Todos os feedbacks registrados em um documento ficam preservados:</p>
            <ul>
                <li>Os feedbacks são exibidos em <strong>ordem cronológica</strong> (mais antigos primeiro)</li>
                <li>Múltiplos feedbacks podem ser registrados no mesmo documento ao longo do tempo</li>
                <li>O histórico completo fica disponível enquanto o documento existir no sistema</li>
                <li>Coordenadores podem ver todos os feedbacks registrados (inclusive por outros coordenadores)</li>
            </ul>
        </div>
    </div>
</div>

<h6 class="fw-bold mt-4 mb-3">Diferença entre Feedback e Solicitação de Revisão</h6>

