<p class="lead">A seção "Feedbacks da Coordenação" permite que coordenadores registrem observações, orientações e elogios diretamente em Observações, Pareceres e Portfólios, com notificação automática ao professor responsável.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Canal de comunicação direta:</strong> Os feedbacks são uma forma de comunicação rápida e registrada entre coordenador e professor, sem precisar usar o fluxo formal de Solicitação de Revisão. O professor recebe notificação imediata ao ser registrado um feedback.
</div>

<h5 class="mt-4 mb-3"><i class="fas fa-comment-alt me-2 text-success"></i>Onde os Feedbacks estão disponíveis</h5>

<p>A seção de Feedbacks da Coordenação aparece na página de visualização dos seguintes documentos:</p>

