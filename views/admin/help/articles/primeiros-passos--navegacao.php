<p class="lead">Aprenda a navegar com eficiência pelo painel, usar o modo escuro e aproveitar os tours interativos.</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Menu Lateral</h6>
            <p>O menu lateral está sempre visível no desktop. Cada seção agrupa funcionalidades relacionadas. O item ativo é destacado com uma borda amarela à esquerda.</p>
            <ul>
                <li>No <strong>celular</strong>, toque no botão ☰ para abrir o menu</li>
                <li>Toque fora do menu ou no overlay escuro para fechar</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Modo Escuro</h6>
            <p>Clique no ícone <i class="fas fa-moon"></i> na barra superior para alternar. A preferência é salva no navegador e persiste entre sessões.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Tours Interativos</h6>
            <p>Cada página possui um tour guiado que destaca e explica cada elemento. Para iniciar:</p>
            <ul>
                <li>Clique no botão <strong>?</strong> na barra superior, ou</li>
                <li>Use o link <strong>"Tour desta Página"</strong> no menu lateral</li>
            </ul>
            <p>O tour mostra um por um os elementos da página com explicações detalhadas e dicas de uso.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Central de Ajuda</h6>
            <p>Você está nela agora! Acesse pelo menu lateral em <strong>Ajuda → Central de Ajuda</strong>. Use a busca para encontrar artigos rapidamente.</p>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> O modo escuro funciona em todas as páginas, incluindo esta Central de Ajuda. Experimente!
</div>
