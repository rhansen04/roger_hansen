    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> O modo escuro funciona em todas as páginas, incluindo esta Central de Ajuda. O atalho de teclado para alternar é clicar no ícone <i class="fas fa-moon"></i> na barra superior.
