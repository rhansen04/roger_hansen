<p class="lead">Aprenda a navegar com eficiência pelo painel, entenda a organização do menu por seções, use o modo escuro e aproveite os tours interativos para dominar cada página.</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Menu Lateral — Organizado por Seções</h6>
            <p>O menu lateral é a navegação principal da plataforma. Ele agrupa os módulos em seções lógicas para facilitar o acesso:</p>
            <ul>
                <li><strong>Cadastros:</strong> Alunos, Escolas, Usuários — gestão de registros base</li>
                <li><strong>Ensino:</strong> Cursos, Matrículas, Turmas, Planejamento, Templates — organização curricular e acadêmica</li>
                <li><strong>Pedagógico:</strong> Observações, Pareceres, Portfólios, Banco de Imagens, Material de Apoio — trabalho pedagógico diário</li>
                <li><strong>Comunicação:</strong> Contatos, Notificações — mensagens e alertas</li>
                <li><strong>Análises:</strong> Relatórios, Dashboard de Vídeos — dados e métricas</li>
                <li><strong>Ajuda:</strong> Central de Ajuda, Tour desta Página — suporte e orientação</li>
            </ul>
            <p>O item ativo no menu é destacado com uma borda colorida à esquerda, indicando em qual página você está.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Menu Adaptado ao seu Papel</h6>
            <p>O menu lateral se adapta automaticamente ao papel do usuário logado:</p>
            <ul>
                <li><strong>Admin:</strong> vê todos os itens do menu, incluindo gestão de usuários, escolas, relatórios completos e configurações</li>
                <li><strong>Professor:</strong> vê apenas os módulos relevantes ao seu trabalho — turmas, observações, pareceres, portfólios, planejamento, banco de imagens e cursos</li>
                <li><strong>Coordenador:</strong> vê os módulos pedagógicos em modo de supervisão, relatórios e gestão de turmas</li>
            </ul>
            <p>Você nunca verá opções que não pode acessar. Se um item não aparece no menu, é porque seu papel não tem permissão para aquele módulo.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Barra Superior — Ferramentas Rápidas</h6>
            <p>A barra no topo da tela oferece acesso rápido a funcionalidades importantes:</p>
            <ul>
                <li><strong>Modo escuro <i class="fas fa-moon"></i>:</strong> alterna entre tema claro e escuro. A preferência é salva no navegador e persiste entre sessões</li>
                <li><strong>Notificações <i class="fas fa-bell"></i>:</strong> ícone de sino com badge numérico indicando quantas notificações não lidas você tem. Clique para ver a lista</li>
                <li><strong>Ajuda <i class="fas fa-question-circle"></i>:</strong> inicia o tour interativo da página atual, destacando cada elemento com explicações</li>
                <li><strong>Menu do usuário:</strong> seu nome e papel são exibidos. Clique para acessar perfil, configurações e sair do sistema</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Navegação no Celular</h6>
            <p>Em dispositivos móveis, o layout se adapta automaticamente:</p>
            <ul>
                <li><strong>Menu hamburger <i class="fas fa-bars"></i>:</strong> toque no ícone no canto superior esquerdo para abrir o menu lateral</li>
                <li><strong>Fechar:</strong> toque fora do menu ou no overlay escuro para fechá-lo</li>
                <li><strong>Tabelas responsivas:</strong> tabelas com muitas colunas ganham rolagem horizontal</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Tours Interativos — Aprenda Fazendo</h6>
            <p>Cada página da plataforma possui um tour guiado que destaca e explica cada elemento. Para iniciar um tour:</p>
            <ul>
                <li><strong>Botão ?</strong> na barra superior — inicia o tour da página atual</li>
                <li><strong>"Tour desta Página"</strong> no menu lateral, na seção Ajuda — mesmo efeito</li>
            </ul>
            <p>O tour mostra um por um os elementos da página com explicações detalhadas, dicas de uso e setas indicando a posição de cada item. Você pode avançar, voltar ou fechar o tour a qualquer momento.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">6</div>
        <div class="step-content">
            <h6>Central de Ajuda</h6>
            <p>Você está nela agora! A Central de Ajuda oferece artigos detalhados sobre cada funcionalidade:</p>
            <ul>
                <li><strong>Acesso:</strong> menu lateral em <strong>Ajuda &rarr; Central de Ajuda</strong></li>
                <li><strong>Busca:</strong> use o campo de busca no topo para encontrar artigos por palavra-chave</li>
                <li><strong>Categorias:</strong> os artigos são organizados por tema (Primeiros Passos, Dashboard, Turmas, Pedagógico, etc.)</li>
                <li><strong>Feedback:</strong> ao final de cada artigo, indique se ele foi útil para nos ajudar a melhorar</li>
            </ul>
        </div>
    </div>
</div>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> O modo escuro funciona em todas as páginas, incluindo esta Central de Ajuda. O atalho de teclado para alternar é clicar no ícone <i class="fas fa-moon"></i> na barra superior.
</div>

