<h6 class="fw-bold mt-4 mb-3">Notificação automática para coordenadores</h6>
<p>Quando o professor clica em <strong>"Finalizar Planejamento"</strong> e o status muda para <span class="badge bg-primary">Enviado</span>, o sistema envia automaticamente uma notificação para <strong>todos os coordenadores</strong> da escola. A notificação inclui:</p>
<ul>
    <li><strong>Nome do professor</strong> que finalizou o planejamento</li>
    <li><strong>Turma</strong> vinculada ao planejamento</li>
    <li><strong>Link direto</strong> para visualizar o planejamento completo</li>
</ul>
<p>Os coordenadores podem acessar o planejamento pela notificação ou pelo menu <strong>Ensino → Planejamentos</strong>, filtrando por status "Enviado".</p>

<h6 class="fw-bold mt-4 mb-3">Filtros por Status</h6>
<p>Na lista de planejamentos (<strong>Ensino → Planejamentos</strong>), use os filtros para localizar planejamentos rapidamente:</p>
<ul>
    <li><strong>Por status:</strong> Rascunho, Enviado ou Registrado</li>
    <li><strong>Por professor:</strong> filtre os planejamentos de um professor específico</li>
    <li><strong>Por turma:</strong> veja os planejamentos de uma turma específica</li>
</ul>
<p>Esses filtros são especialmente úteis para coordenadores acompanharem as entregas pendentes e para administradores terem uma visão geral do andamento dos planejamentos da escola.</p>

<div class="help-tip help-tip-success">
    <i class="fas fa-check-circle me-2"></i>
    <strong>Resumo:</strong> O fluxo é simples e linear — <strong>Rascunho</strong> (professor preenche dias) → <strong>Enviado</strong> (coordenação notificada, registro disponível) → <strong>Registrado</strong> (ciclo concluído, acervo permanente). Cada transição é feita por um único botão, sem necessidade de aprovação externa.
</div>
