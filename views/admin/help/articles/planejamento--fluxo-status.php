<h6 class="fw-bold mt-4 mb-3">Filtros por Status</h6>
<p>Na lista de planejamentos (<strong>Ensino → Planejamentos</strong>), use os filtros para visualizar planejamentos por status, professor ou turma. Isso é especialmente útil para a coordenação acompanhar entregas pendentes.</p>

<div class="help-tip help-tip-success">
    <i class="fas fa-check-circle me-2"></i>
    <strong>Dica:</strong> Professores veem apenas seus próprios planejamentos. Administradores e coordenadores veem todos.
</div>
