<p class="lead">Cada planejamento segue um fluxo de status que reflete as etapas do processo pedagógico: criação, revisão e registro final.</p>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Rascunho <span class="badge bg-warning text-dark ms-2">Rascunho</span></h6>
            <p>Estado inicial de todo planejamento. Nesta fase:</p>
            <ul>
                <li>Apenas o professor autor pode ver e editar</li>
                <li>Pode ser editado livremente quantas vezes quiser</li>
                <li>Não é visível para a coordenação</li>
                <li>Ideal para ir preenchendo aos poucos antes de enviar</li>
            </ul>
            <p><strong>Ações disponíveis:</strong> Editar, Salvar, Enviar para revisão</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Enviado <span class="badge bg-primary ms-2">Enviado</span></h6>
            <p>O professor considera o planejamento pronto e envia para a coordenação:</p>
            <ul>
                <li>Sinaliza que o planejamento está completo e pronto para avaliação</li>
                <li>A coordenação pode visualizar e analisar o conteúdo</li>
                <li>O professor ainda pode editar se necessário</li>
                <li>Útil para acompanhamento de entregas pela gestão</li>
            </ul>
            <p><strong>Ações disponíveis:</strong> Editar, Registrar (pós-vivência)</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Registrado <span class="badge bg-success ms-2">Registrado</span></h6>
            <p>Estado final — o planejamento foi vivenciado e registrado:</p>
            <ul>
                <li>Representa o <strong>"Registro Pós-Vivência"</strong></li>
                <li>O professor complementa com observações de como a prática aconteceu</li>
                <li>Serve como documentação final para a coordenação e acervo pedagógico</li>
                <li>Estado final — não avança mais</li>
            </ul>
        </div>
    </div>
</div>

