<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Rascunho <span class="badge bg-warning text-dark ms-2">Rascunho</span></h6>
            <p>Estado inicial de todo planejamento. O professor preenche os dias livremente na grade quinzenal:</p>
            <ul>
                <li><strong>Grade de dias:</strong> os cards de dia são clicáveis e editáveis. Cada card mostra seu status individual (Vazio, Rascunho ou Preenchido)</li>
                <li><strong>Edição livre:</strong> o professor pode preencher, salvar, editar e reescrever qualquer dia quantas vezes quiser</li>
                <li><strong>Ordem flexível:</strong> os dias podem ser preenchidos em qualquer ordem, sem obrigar uma sequência cronológica</li>
                <li><strong>Visibilidade:</strong> o planejamento em rascunho é visível apenas para o professor autor e para administradores</li>
                <li><strong>Coordenação:</strong> coordenadores <strong>não</strong> recebem notificação neste estágio</li>
            </ul>
            <p><strong>Ações disponíveis:</strong> Preencher cards diários, Salvar dias, Ver Completo, Rotina Semanal, <strong>Finalizar Planejamento</strong></p>
            <p><strong>Transição:</strong> ao clicar em <strong>"Finalizar Planejamento"</strong> (botão na parte inferior da grade de dias), o status avança para Enviado.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Enviado <span class="badge bg-primary ms-2">Enviado</span></h6>
            <p>O professor considera o planejamento pronto e o envia. A coordenação é notificada automaticamente:</p>
            <ul>
                <li><strong>Notificação automática:</strong> todos os coordenadores da escola recebem uma notificação com o nome do professor e a turma do planejamento finalizado</li>
                <li><strong>Cards diários:</strong> tornam-se <strong>somente leitura</strong> — o professor pode visualizar os dias, mas não pode mais editar</li>
                <li><strong>Registro Pós-Vivência:</strong> o botão <strong>"Registro Pós-Vivência"</strong> aparece na grade de dias, permitindo ao professor registrar reflexões sobre como as atividades se concretizaram</li>
                <li><strong>Visibilidade:</strong> o planejamento agora é visível para o professor, coordenadores e administradores</li>
                <li><strong>Acompanhamento:</strong> a coordenação pode abrir o planejamento e revisar todo o conteúdo preenchido dia a dia</li>
            </ul>
            <p><strong>Ações disponíveis:</strong> Visualizar cards diários (somente leitura), Ver Completo, <strong>Registro Pós-Vivência</strong> (Salvar Rascunho / Finalizar Registro)</p>
            <p><strong>Transição:</strong> ao clicar em <strong>"Finalizar Registro"</strong> no formulário de Registro Pós-Vivência, o status avança para Registrado.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Registrado <span class="badge bg-success ms-2">Registrado</span></h6>
            <p>Estado final do planejamento — o ciclo está completo:</p>
            <ul>
                <li><strong>Registro concluído:</strong> o professor preencheu o Registro Pós-Vivência com reflexões e sínteses sobre o período</li>
                <li><strong>Tudo somente leitura:</strong> cards diários ficam não clicáveis, campos do registro ficam bloqueados</li>
                <li><strong>Badge visual:</strong> <span class="badge bg-success">Planejamento Concluído</span> aparece na barra de informações da grade de dias</li>
                <li><strong>Estado permanente:</strong> não há transição a partir deste status — o planejamento se torna documentação definitiva do acervo pedagógico</li>
                <li><strong>Visibilidade:</strong> continua visível para o professor, coordenadores e administradores como registro histórico</li>
            </ul>
            <p><strong>Ações disponíveis:</strong> Apenas visualização completa</p>
        </div>
    </div>
</div>

