<p class="lead">Cada planejamento pedagógico segue um fluxo de três status que reflete o ciclo completo: criação e preenchimento dos dias, envio para a coordenação, e registro pós-vivência. Entenda cada etapa, quem vê o quê e quais ações estão disponíveis em cada momento.</p>

