<p class="lead">A edição do portfólio é organizada em abas (pills), onde você preenche textos institucionais, a mensagem da professora e, para cada um dos 5 eixos de atividade, escreve uma descrição e adiciona fotos com legendas.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Dica:</strong> Navegue entre as abas livremente e salve o progresso a qualquer momento. Você não precisa preencher tudo de uma vez.
</div>

<div class="article-steps">
    <div class="article-step">
        <div class="step-number">1</div>
        <div class="step-content">
            <h6>Abra o portfólio em modo de edição</h6>
            <p>Na listagem de portfólios, clique no portfólio desejado e depois em <strong>"Editar"</strong>. A página de edição utiliza uma interface de <strong>abas em formato pill</strong> para organizar as seções:</p>
            <ul>
                <li><strong>Capa</strong> — Foto de capa da turma</li>
                <li><strong>Sobre a Magia</strong> — Texto institucional (somente leitura)</li>
                <li><strong>Proposta</strong> — Texto institucional (somente leitura)</li>
                <li><strong>Mensagem</strong> — Mensagem da professora (editável)</li>
                <li><strong>Eixos Intro</strong> — Texto introdutório dos eixos (somente leitura)</li>
                <li><strong>Movimento</strong>, <strong>Manual</strong>, <strong>Musical</strong>, <strong>Contos</strong>, <strong>PCA</strong> — Os 5 eixos (editáveis)</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">2</div>
        <div class="step-content">
            <h6>Textos institucionais fixos</h6>
            <p>Algumas abas contêm textos institucionais padronizados que <strong>não podem ser editados</strong>, pois são os mesmos para todos os portfólios:</p>
            <ul>
                <li><strong>"Sobre a Magia do Portfólio":</strong> Texto que explica às famílias a importância e o significado do portfólio como registro pedagógico</li>
                <li><strong>"Proposta da Pedagogia Florença":</strong> Apresenta os 5 princípios fundamentais da metodologia pedagógica, exibidos em caixas estilizadas</li>
                <li><strong>"Os Eixos de Atividades":</strong> Texto introdutório que apresenta os eixos de atividade antes das páginas detalhadas de cada um</li>
            </ul>
            <p>Esses textos garantem a <strong>identidade visual e pedagógica</strong> consistente em todos os portfólios da escola.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Aba "Mensagem" — Mensagem da Professora</h6>
            <p>Nesta aba, edite a mensagem da professora para as famílias:</p>
            <ul>
                <li>Escreva ou revise o texto de saudação e reflexão sobre o semestre</li>
                <li><strong>Correção por IA:</strong> Clique no botão <strong>"Corrigir texto"</strong> para enviar a mensagem ao Gemini</li>
                <li>A IA corrige ortografia, gramática e fluidez, preservando o conteúdo e o tom da professora</li>
                <li>O texto corrigido pode ser editado manualmente após a correção</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Abas dos 5 Eixos — Descrição e Fotos</h6>
            <p>Cada eixo de atividade possui sua própria aba com dois blocos de conteúdo:</p>

            <p><strong>Bloco 1 — Descrição do eixo (textarea):</strong></p>
            <ul>
                <li>A professora escreve um texto descrevendo as atividades realizadas pela turma naquele eixo durante o semestre</li>
                <li>Detalhe as vivências, os materiais utilizados, as conquistas do grupo e os momentos marcantes</li>
                <li>Esse texto aparecerá em uma página dedicada no PDF, antes da página de fotos do eixo</li>
            </ul>

            <p class="mt-3"><strong>Bloco 2 — Fotos (até 3 por eixo) — exclusivamente do Banco de Imagens:</strong></p>
            <ul>
                <li>Cada eixo possui <strong>3 slots de foto</strong></li>
                <li>As fotos são selecionadas <strong>exclusivamente a partir do Banco de Imagens interno</strong> da turma — o upload direto no portfólio foi removido</li>
                <li>Para cada slot, clique no botão <strong>"Selecionar do Banco de Imagens"</strong> — um modal exibe as fotos disponíveis na turma para seleção</li>
                <li>Após selecionar a foto, adicione a <strong>legenda</strong> descritiva que aparecerá abaixo da imagem no PDF</li>
                <li>As fotos devem representar <strong>atividades coletivas</strong> da turma no eixo</li>
                <li>Não é obrigatório preencher os 3 slots — use quantas fotos forem adequadas</li>
            </ul>

            <div class="help-tip">
                <i class="fas fa-lightbulb me-2"></i>
                <strong>Pré-requisito:</strong> Certifique-se de que as fotos da turma já foram enviadas ao <strong>Banco de Imagens</strong> antes de montar o portfólio. Acesse <strong>Banco de Imagens</strong> no menu para fazer o upload.
            </div>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Os 5 eixos de atividade</h6>
            <p>Os eixos seguem a organização da Pedagogia Florença:</p>
            <ul>
                <li><strong>Atividade de Movimento:</strong> Brincadeiras corporais, dança, circuitos motores, jogos cooperativos, exploração do espaço</li>
                <li><strong>Atividade Manual:</strong> Pintura, colagem, modelagem em argila, desenho, trabalhos com materiais diversos</li>
                <li><strong>Atividade Musical:</strong> Cantigas de roda, exploração de instrumentos, ritmo, musicalização, escuta ativa</li>
                <li><strong>Atividade de Contos:</strong> Contação de histórias, dramatizações, fantoches, reconto, criação de narrativas</li>
                <li><strong>Programa Comunicação Ativa (PCA):</strong> Rodas de conversa, desenvolvimento da linguagem oral, projetos de comunicação</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">6</div>
        <div class="step-content">
            <h6>Salve e visualize o resultado</h6>
            <p>Após preencher as descrições e fotos dos eixos:</p>
            <ul>
                <li>Clique em <strong>"Salvar"</strong> para gravar todas as alterações</li>
                <li>Na página de visualização (show), todas as seções são exibidas em sequência, simulando a ordem do PDF</li>
                <li>Verifique se os textos estão coerentes e se as URLs das fotos estão funcionando</li>
                <li>Você pode voltar a editar quantas vezes forem necessárias enquanto o portfólio estiver em rascunho</li>
            </ul>
        </div>
    </div>
</div>

