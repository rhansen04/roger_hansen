<p class="lead">Ao abrir um planejamento para edição, você verá a nova visualização quinzenal: uma grade de cards organizados por semana, onde cada dia da semana (segunda a sexta) é representado por um card com status visual. Essa visão facilita o preenchimento dia a dia do planejamento pedagógico.</p>

<div class="help-tip">
    <i class="fas fa-lightbulb me-2"></i>
    <strong>Novidade R2-05:</strong> A grade de dias substitui o formulário único antigo. Agora cada dia letivo tem seu próprio card e formulário individual, permitindo um planejamento mais organizado e granular.
</div>

<h6 class="fw-bold mt-4 mb-3">Barra de informações do planejamento</h6>
<p>No topo da página de dias, uma barra de informações exibe os dados principais do planejamento:</p>
<ul>
    <li><strong>Template:</strong> nome do template utilizado (ex.: "Planejamento Semanal Infantil")</li>
    <li><strong>Turma:</strong> turma vinculada ao planejamento</li>
    <li><strong>Período:</strong> data de início e fim (ex.: 03/03/2026 a 14/03/2026)</li>
    <li><strong>Status:</strong> badge com o status atual — <span class="badge bg-warning text-dark">Rascunho</span>, <span class="badge bg-primary">Enviado</span> ou <span class="badge bg-success">Registrado</span></li>
</ul>

<h6 class="fw-bold mt-4 mb-3">Botões de ação rápida</h6>
<p>Abaixo da barra de informações, dois botões de ação rápida estão disponíveis:</p>
<ul>
    <li><strong>"Ver Completo":</strong> abre uma visão consolidada de todos os dias preenchidos em uma única página, ideal para revisão antes de finalizar</li>
    <li><strong>"Rotina Semanal":</strong> exibe o calendário de rotina semanal definido para a turma, útil como referência durante o preenchimento</li>
</ul>

