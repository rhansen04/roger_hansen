            <ul>
                <li><strong>Dia da semana abreviado:</strong> Seg, Ter, Qua, Qui, Sex</li>
                <li><strong>Número do dia:</strong> ex.: 03, 04, 05</li>
                <li><strong>Mês:</strong> ex.: Mar, Abr</li>
                <li><strong>Ícone de status:</strong> indica se o dia foi preenchido ou não</li>
            </ul>
            <p>Os dias são agrupados por semana, com linhas horizontais separando cada semana para facilitar a leitura visual.</p>
