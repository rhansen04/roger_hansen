<div class="article-steps">
    <div class="article-step">
        <div class="step-number">3</div>
        <div class="step-content">
            <h6>Destaque do dia atual</h6>
            <p>O card correspondente à data de hoje recebe um destaque visual especial: um badge azul <span class="badge bg-primary">HOJE</span> aparece sobre o card, facilitando a identificação rápida do dia corrente na grade.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">4</div>
        <div class="step-content">
            <h6>Navegando entre os dias</h6>
            <p>Para preencher um dia específico, basta <strong>clicar no card</strong> correspondente. O sistema abrirá o formulário diário com os campos do template para aquele dia. Ao salvar, você retorna automaticamente à grade de dias.</p>
            <p>Você pode preencher os dias em qualquer ordem — não é necessário seguir a sequência cronológica.</p>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">5</div>
        <div class="step-content">
            <h6>Finalizar Planejamento</h6>
            <p>Na parte inferior da grade de dias, o botão <strong>"Finalizar Planejamento"</strong> está disponível quando o status é <span class="badge bg-warning text-dark">Rascunho</span>. Ao clicar:</p>
            <ul>
                <li>O status muda para <span class="badge bg-primary">Enviado</span></li>
                <li>Os coordenadores são notificados automaticamente</li>
                <li>Os cards de dia tornam-se <strong>somente leitura</strong></li>
                <li>O botão "Finalizar Planejamento" desaparece</li>
                <li>O botão <strong>"Registro Pós-Vivência"</strong> aparece no lugar</li>
            </ul>
        </div>
    </div>

    <div class="article-step">
        <div class="step-number">6</div>
        <div class="step-content">
            <h6>Após o registro (status Registrado)</h6>
            <p>Quando o professor completa o Registro Pós-Vivência e o status muda para <span class="badge bg-success">Registrado</span>:</p>
            <ul>
                <li>Um badge <span class="badge bg-success">Planejamento Concluído</span> aparece na barra de informações</li>
                <li>Todos os cards de dia ficam <strong>não clicáveis</strong> (somente visualização)</li>
                <li>Nenhum botão de ação aparece na parte inferior — o planejamento está encerrado</li>
            </ul>
        </div>
    </div>
</div>

